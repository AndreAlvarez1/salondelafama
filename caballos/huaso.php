			<div class="bio col s12">
		
<p>El caballo “Huaso” (ex “Faithful”), sin lugar a dudas es el caballo más conocido del deporte ecuestre nacional. Nacido en septiembre de 1933 y fallecido el 24 de agosto de 1961.</p>

<p>“Huaso” fue un caballo FSI chileno, con el cual el capitán del Ejército Alberto Larraguibel Morales batió la marca mundial de salto alto de equitación el 5 de febrero de 1949, en el marco de un concurso ecuestre internacional oficial celebrado en el Regimiento Coraceros de Viña del Mar. Allí logró un brinco de 2,47 metros (8 ft 1¼ in) de altura, quebrando la marca anterior de 2,44 m (8 ft 0 in), establecido por el jinete italiano Antonio Gutiérrez y el caballo “Ossopo” en 1938.</p>

<p>Nacido en Chile en 1933, “Huaso” era un caballo colorado purasangre de 1,68 metros altura, perteneciente a el criadero "Las Mañanas". Fue bautizado como “Faithful” y fue hijo de “Henry Lee” y “Trémula”, para entonces reconocidos caballos de carreras.</p>

<p>De "Faithful" se esperaba el mismo desempeño de los padres, pero los dueños encontraron que era un caballo sin mucha proyección en las carreras en el hipódromo. De los caballos de aquel año ya estaban compitiendo y la gran mayoría ganando, mientras “Huaso” no tenía una sola carrera ganada en el cuerpo, por lo tanto, su venta se precipitó.</p>

<p>“Huaso” fue vendido a un club de polo, pero fue regresado rápidamente al ver que el potro no soportaba el duro entrenamiento y el golpe de los tacos. El animal fue comprado por el Ejército de Chile y fue iniciado por el capitán Gaspar Lueje en adiestramiento, con regulares resultados. A poco andar, un accidente – se enterró un fierro en una de sus nalgas- provocó su retiro inmediato de esta disciplina y casi fue ordenado su sacrificio.</p>

<p>Tras ese hecho, el capitán Alberto Larraguibel Morales se cruzó en su camino, primero redirigiéndolo a la disciplina de salto y luego cambiándole el nombre de “Faithful” a “Huaso”.</p>

<p>Al principio de su entrenamiento tampoco era un caballo fácil y menos dócil, ante lo cual no fue tomado muy en cuenta en el equipo institucional y le costaba mucho saltar en los recorridos. La principal dificultad la tenía en las agrupaciones. El capitán Larraguibel decidió entrenarlo en salto alto, a sugerencia de su maestro el Mayor Rafael Monti Roa.</p>

<h3>El récord mundial de salto alto</h3>


<p>En 1948 Alberto Larraguibel, con “Huaso”, bate el récord sudamericano de 2,37 metros de altura. En 1949 concursaban dos binomios por el récord mundial. Fueron el capitán Alberto Larraguibel y “Huaso”, y el teniente Ernesto Riquelme y su caballo “Chileno”, pero estos se desplomaron sobre un salto dejando la pasada a “Huaso” y Alberto Larraguibel.</p>


<p>Al primer intento de pasar el salto, el caballo rehusó, negándose a pasar las varas; al segundo intento, “Huaso” pasó las manos, pero rozó con el vientre botando una de las varas. En el tercer y último intento, pasaron bien, debido a la decidida monta del jinete y asombro del público. La gente presente ese día, dice que cuando el binomio estaba en su último intento, era un silencio palpable. Cuando el animal inició el vuelo, nadie respiraba, y cuando sus patas delanteras tocaron el suelo, fue un completo griterío. Gente gritaba, gente lloraba, inclusive se acercaron a “Huaso” para arrancarle crines como recuerdo. La banda del Ejército empezó a tocar el himno nacional, apenas el caballo tocó el suelo.</p>


<p>Después de batir el récord mundial de salto alto, “Huaso” continuó su vida competitiva, incluso viajó a Europa y a su regreso siguió participando en la alta competencia. Posteriormente pasó a un merecido descanso en la Escuela de Caballería del Ejército en Quillota (actual Escuela de Equitación), donde tendría el derecho de pasearse por los prados y jardines sin que nadie lo montara hasta el día de su muerte, el 24 de agosto de 1961, a los 28 años. Actualmente sus restos descansan en la misma Escuela de Equitación del Ejército en Quillota. Su hazaña no ha sido superada aún, manteniendo el récord mundial de salto alto en equitación.
</p>


<h3>Después del récord</h3>
<p>Se ha intentado reiteradas veces romper el récord de “Huaso”, pero hasta hoy no se ha logrado. A fines de 2007 se instaló un monumento en su recuerdo ubicado en la avenida Jorge Montt de Viña del Mar, cercano al reloj de flores. Existen libros que relatan la vida de “Huaso”, uno titulado Caballo Loco campeón del mundo, de Luis Alberto Tamayo, y otro Quique Hache y el caballo fantasma, de Sergio Gómez. También está El Jinete Alado, del periodista e historiador quillotano Roberto Silva Bijit.</p>

<h3>Frases célebres de Alberto Larraguibel</h3>
<p>“Me dirigí a la cancha y en la entrada noté mucho nerviosismo, todos me apuraban. Me fui a la nave y ordené al soldado caballerizo que me siguiera e hiciera todo lo que yo le ordenara. Le dije: dale una vuelta al caballo alrededor de la pista y déjalo y que coma algunas hojas si quiere. Así lo hizo, y el caballo, nervioso, le dio tarascones a las matas de cardenal que cuidadosamente adornaban el recinto. Dimos una vuelta más, mientras todos insistían en que yo montara. Deseaba que el caballo viera al público y se tranquilizara. Ante tanta insistencia, no me quedó más que subir a mi cabalgadura, lo que hice con toda calma”.</p>

<p>“En el primer intento “Huaso” rehusó. Si lo hubiera "huasqueado" se habría puesto nervioso, porque un animal se da cuenta cuando se le está pidiendo algo superior a sus fuerzas. En el segundo intento debo haberme equivocado en un centímetro, porque “Huaso” pasó las manos, pero rozó con el vientre y las patas, botando la vara”.</p>

<p>“Volví a desmontar y a medir las distancias, una y dos veces; el Mayor Montti se colocó en un lugar determinado para dar el 'ya' desde donde debía empezar a apurar el caballo. Lo conduje por última vez frente al salto, le hablé, lo acaricié, le transmití toda mi fuerza y mi fe y le dije: “Huaso”, esta es nuestra oportunidad. Ahora o nunca. Entonces picó justo donde me lo había fijado y en un accionar mágico de elasticidad, potencia, decisión y armonía, voló por el aire, salvando el obstáculo con cero faltas”.</p>




