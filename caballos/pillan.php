			<div class="bio col s12">
		
<p>El caballo “Pillán” fue uno de los caballos de mayor trayectoria en la equitación nacional. Fue un caballo criado en Chile, nació el 8 de noviembre de 1943, en el Fundo Santa Marta, cerca de la ciudad de Victoria, en la Novena Región.</p>

<p>Sus padres fueron la yegua mestiza fina sangre, “Dulzura” y el potro fina sangre inglés “Orégano”. Fue adiestrado en el Regimiento Cazadores en Santiago y montado por los mejores jinetes del Ejército, en la época de oro de la equitación nacional, también por jinetes de Carabineros entre ellos el General César Mendoza Durán en los Juegos Olímpicos de Helsinki en 1952.</p>

<p>Este caballo con extraordinarias capacidades para el salto participó en muchos eventos ecuestres en el país y en el extranjero, formando parte del equipo de la Escuela de Caballería.</p>

<p>“Pillán” fue destacado en competencias internacionales en ciudades europeas, en Estados Unidos, en los Juegos Panamericanos de Buenos Aires el año 1951, en Ciudad de México en 1955, Chicago en el año 1959 y en los Juegos Olímpicos de Helsinki en 1952.</p>

<p>También fue montado por destacados jinetes internacionales que participaron en concursos internacionales que se realizaron en Viña del Mar y Santiago, como Piere D Oriola; Cecil BLecke de Inglaterra, Hugo Rambide de Argentina y Hans Schokhemehler de Alemania. Este gran caballo fue uno de los artífices de la medalla de oro en los Panamericanos del 1951, y de la medalla de plata Helsinki 1952, medalla de bronce en México 1955 y medalla de bronce en Chicago 1959.</p>

<p>También ganó en el Madison Square Garden, Nueva York, y en Düsseldorf, Alemania en 1952. Ganó además en Holanda y Austria pruebas de potencia a más de dos metros de altura y fue cuarto en Aachen, Alemania, con el capitán Guillermo Aranda. Este noble ejemplar prolongó su trayectoria de triunfos por casi 20 años, siendo su último jinete el Mayor Luis Santos Fernández, quién lo retiró de las pistas en 1963.</p>

<p>Los últimos años de su vida estuvo siempre libre en los prados de la Escuela de Caballería en Quillota. Dejó de existir un 19 de septiembre de 1973, día en que se celebran las Glorias del Ejército de Chile, siendo sus restos sepultados en el cementerio de caballos de este instituto castrense.</p>

<p>“Pillán” el recuerdo de tu nombre grabado en mármol y tu espíritu como ser mitológico perdurará entre la familia ecuestre, no dejaremos que el polvo del olvido cubra las huellas de tus pasos sobre las rutas victoriosas.</p>

