			<div class="bio col s12">
		
<p>Eduardo Yáñez Zavala nació en Santiago el 15 de abril de 1903. Fue Oficial de Ejército y un gran maestro de equitación. Durante décadas fue jefe del equipo de Chile en diferentes concursos internacionales. </p>
 

<p>Estudió en la Escuela Militar del General Bernardo O´Higgins y después de una larga y destacada carrera militar, llegó a ser General de Brigada en 1953 y luego General de División. También ocupó cargos políticos, como Ministro de Obras Públicas y del Trabajo en el Gobierno de don Carlos Ibáñez del Campo y Embajador de Chile en Países Bajos, en la década de 1970.</p>
 

<p>Fue reconocido internacionalmente como gran deportista, respetado juez internacional y ejemplar dirigente. Se desempeñó como maestro en la Escuela de Caballería de Quillota. También fue instructor del Instituto Ecuestre de Adiestramiento de Alemania y presidió durante varios períodos la Federación Ecuestre de Chile.</p>
 

<p>Influyó en el gusto por el caballo e inculcó la doctrina Alemana que primaba a principios del siglo XX. Su hermano, don Elías Yáñez, en Estocolmo 1912, fue el primer chileno en participar en la disciplina de salto en unos Juegos Olímpicos.</p>
 

<p>En uno de los tantos homenajes recibidos fue condecorado por el Rey de Suecia. En 1981, reunido en la ciudad alemana de Baden, el Comité Olímpico Internacional junto a la FEI , lo distinguieron como El Mejor Jinete del Mundo. Era conocido en el mundo ecuestre como “El Maestro”.</p>
 

<p>Este premio lo ganó principalmente por obtener cinco veces el Gran Premio en los concursos internacionales de Madison Square Garden en Nueva York. Este ha sido el principal premio que ha recibido un deportista chileno en la historia.</p>
 

<p>Eduardo Yáñez perteneció a una generación de oro y de destacados equitadores chilenos como Hernán Vigil, Ricardo Echeverría, Alberto Larraguibel, Óscar Cristi y César Mendoza.</p>
 

<p>Durante sus mandatos como presidente de la FECH, nuestro equipo nacional tuvo destacadas participaciones en Sudamericanos, Juegos Panamericanos y Juegos Olímpicos.</p>

