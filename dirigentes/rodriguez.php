			<div class="bio col s12">
		
<p>Nace en la ciudad de Niza (Francia) el 22 de abril de 1918. Hijo del Coronel de caballería, don Benjamín Rodríguez Artigas y doña Elsa Véliz. Por su padre heredó
el gusto por el caballo y practicó la equitación desde muy niño.
Es conocido en la familia ecuestre por su sobrenombre de “Polaco”, debido a que cuando nació, su padre ganaba el Gran Prix de Varsovia – Polonia. Hizo sus
primeros estudios de enseñanza básica en el Colegio Alemán de Santiago y Valparaíso.</p>

<p>En 1945 ingresa a la Escuela Militar graduándose de Oficial de caballería en el grado de Alférez el año 1947. En 1952 contrajo matrimonio con la señora María
Victoria Barahona Izquierdo, con quien tuvo cinco hijos.</p>

<p>Su primera destinación fue en el Regimiento de Caballería “Cazadores” en Santiago y en 1940 es destinado a la Escuela de Caballería en Quillota. Siempre
ocupó el 1º lugar en los Cursos de Tenientes, Curso de Maestros de Equitación y de Capitanes.</p>

<p>Posteriormente, ingresa a la Academia de Guerra del Ejército y siendo alumno de tercer año pide su renuncia voluntaria de la Institución, con 22 años de servicio.
En 1966 logra su primer título internacional al consagrarse junto a su yegua “Rumba” campeón del Sudamericano de salto de Río de Janeiro. El mismo año es
elegido por el Circulo de Periodistas Deportivos como el mejor “Deportista en Equitación”.</p>

<p>Este destacado jinete, maestro y dirigente, en sus múltiples destinaciones a lo largo de nuestro país, siempre fomentó la práctica de la equitación, formando clubes en Punta Arenas, Antofagasta y otras ciudades.
Por su paso por Angol, como comandante del Regimiento Húsares, divulgó y entusiasmó a muchas generaciones de oficiales y civiles para practicar este
hermoso deporte.</p>

<p>Una vez en retiro de la institución se quedó a vivir en la ciudad de Angol, donde continuó con la práctica y difusión de nuestro deporte para entusiasmar a las
nuevas generaciones de jinetes sureños.
En 1976 es nominado como jefe del equipo de Chile que obtuvo el primer lugar en el Campeonato Sudamericano organizado en Santiago. Posteriormente es nominado jefe de equipo de la delegación de Chile para asistir a competencias internacionales realizadas en España y Alemania.</p>

<p>En 1979 es nombrado embajador de Chile en Austria, donde también es representante y delegado de Chile ante la FEI, efectuando una ardua labor
dirigencial.</p>

<p>En 1984, ya de regreso a Chile, es elegido por dos períodos como miembro del directorio de la FEI, hasta el año 1992. En este periodo obtiene la nominación de
juez internacional de salto, lo cual lo califica para juzgar en Juegos Olímpicos y continentales.</p>

<p>En 1994 es elegido miembro del Comité de Saltos de la FEI, hasta el año 1998, implementando el sistema de coach y capacitación de la FEI. Ese mismo año es
elegido nuevamente integrante del directorio de la FEI, manteniendo este importante cargo hasta el año 2002.</p>

<p>A pesar de tener importantes actividades y responsabilidades en la FEI, siempre continuó aportando con su experiencia y asesorías a la FECH, como también
efectuando traducciones de reglamentos, escribiendo artículos y participando como juez en todos los concursos internacionales en nuestro país y en el extranjero.</p>

<p>Actualmente se encuentra viviendo en Codegua, VI Región, y pese a la lejanía la equitación nacional siempre cuenta con sus consejos y atinadas opiniones, lo cual
se valora en toda su extensión y dimensión por el directorio de la FECH.</p>

