			<div class="bio col s12">
		
<p>Este distinguido jinete y dirigente nacional se acerca e ingresa al mundo ecuestre cuando por motivos de trabajo se produce su traslado a la ciudad de Lima, Perú.</p>

<p>En esos años Rafael Montti era profesor de equitación en el club Huachipa de Lima, con el cual creó una gran amistad que perduró por muchos años. El General de Carabineros Manuel Garrido, casado con Patricia Wainer, sobrina de Rolando lo contacta con Rafael Montti, y es a través de él que comienza a montar en el club donde era maestro. </p>

<p>Su primer caballo era una yegua que compró en Perú que se llamaba “Ñusta”, y que se decía que venía de las caballerizas de la Reina Isabel II de Inglaterra.</p>

<p>En Lima parte el gusto por organizar eventos en torno a la equitación, concursos internacionales, acompañado de hermosos eventos sociales. Volviendo a Chile en 1969, por su amistad y parentesco político con Manuel Garrido, sigue montando, pero en la Escuela de Carabineros en la calle Antonio Varas. </p>

<p>El año 70, también por motivos profesionales se trasladó a Buenos Aires, Argentina, donde se hace socio del Club Hípico Argentino y conoce a su segundo maestro, el Coronel Carlos Rusti.</p>

<p>En 1975 regresa a Chile y vuelve a montar en la Escuela de Carabineros en Antonio Varas, con Rafael Montti como profesor y deja el salto para meterse de lleno en el adiestramiento.</p>

<p>Lamentablemente, dos operaciones a las caderas, producto del trote, otra pasión que tenía, lo alejaron de los caballos y lo acercaron paulatinamente a la dirigencia, postulándose al directorio de la FECH el año 1993, siendo elegido presidente, hasta el año 1999. </p>

<p>Su gran capacidad de trabajo, dedicación, entusiasmo y ningún interés por lucir en forma personal y solo por el bien de la Federación Ecuestre de Chile, lo distinguieron y fue un gran aporte a la equitación nacional.</p>

<p>Bajo su dirigencia se hicieron concursos internacionales en la Estación Mapocho, Alto Las Condes, se concurrió a los Juegos Panamericanos de Buenos Aires y Winnipeg, como también, se consiguieron gracias a su gestión grandes auspicios, que financiaron la competencia internacional de nuestros equipos.</p>

<p>En su estadía en Lima y Buenos Aires, siempre recibió a los equipos nacionales con mucho cariño y esmero, lo que recuerdan todos los jinetes que pasaron por su casa.</p>


