			<div class="bio col s12">
		
<p>Hijo de don Belisario Gálvez y doña Paulina Fuschlocher, nació el 16 de diciembre de 1896 en Santiago. Ingresa como Cadete a la Escuela Militar en 1913.</p>

<p>En 1918 se gradúa como oficial de caballería con el grado de teniente 2° y es destinado al Regimiento de Caballería Cazadores a Caballo con guarnición en Santiago.</p>

<p>En 1921 es destinado al regimiento Guías en Concepción destacando como jinete de grandes condiciones.</p>

<p>En 1922 es destinado a la Escuela de Aplicación de Caballería en Santiago a realizar el curso de Maestro de Equitación, alcanzando este título en 1924, después de dos años de especialización con grandes maestros graduados en Alemania y Francia.</p>

<p>En 1925 contrae matrimonio con la señora Julia Wood Silva, ese mismo año pasa en comisión de servicio al Ministerio del Interior y es designado como instructor de caballería en la Policía de Santiago.</p>

<p>En 1926 asciende al grado de capitán y es trasladado al Regimiento de Caballería Dragones, con asiento en Curicó.</p>

<p>En 1927 es trasladado nuevamente a la escuela de Aplicación de Caballería en Santiago como Instructor en el curso de Equitación.</p>

<p>En 1928 participa como integrante del equipo nacional de salto en los concursos ecuestres internacionales en Madrid y Sevilla y posteriormente el Lisboa, donde obtiene importantes triunfos.</p>

<p>En 1929 es destinado al regimiento Granaderos a Caballo con asiento en la ciudad de Iquique.</p>

<p>En 1930 por sus grandes condiciones de jinete y destacado oficial, es comandado a integrar el regimiento Húsares de la Princesa en Madrid España, donde se especializa y continua participando en forma destacada dejando en alto la bandera de nuestro país.</p>

<p>En 1931 es comandado al 6° Regimiento de Caballería en París, Francia, donde continua su desarrollo deportivo y profesional.</p>

<p>En 1932 ingresa a la Academia de Guerra como alumno regular, después de tres largos años de estudio se gradúa como Oficial de Estado Mayor.</p>

<p>En 1935 asciende al grado de Mayor, siempre en la senda de los caballos y de la difusión de la equitación moderna aprendida en Europa.
Este mismo año Integra la comisión Internacional que controla el alto al fuego de la Guerra del Chaco entre Paraguay y Bolivia. En 1938 asciende al grado de Teniente Coronel siendo destinado al Estado Mayor de la División de Caballería con asiento en Santiago.</p>

<p>En 1939 presidió la Comisión Especial de Reglamentación de Caballería, entre estos trabajos le toca redactar el nuevo reglamento de equitación del ejército el que influye notablemente en los nuevos maestros que difundieron la equitación en Chile.</p>

<p>En 1940 es destinado en comisión de servicio a la cancillería por dos años por su capacidad profesional como Oficial de Estado Mayor y dominio del idioma francés.</p>

<p>En 1943 es designado como Agregado Militar en México.</p>

<p>En 1944 es designado para comandar el regimiento de Caballería N° 4 Coraceros en Viña del Mar por tres años. Bajo su mando se desarrolló notablemente la equitación en esta ciudad y particularmente el equipo ecuestre de este regimiento, consolidándose los famosos concursos de verano en el jardín de salto de esta unidad.</p>

<p>En 1948 es nombrado Director de la Escuela de Caballería en la ciudad de Quillota, donde planifica intentar los récord vigentes de salto a caballo.</p>

<p>En 1949 después de un largo trabajo en equipo, dirigidos por este extraordinario director, el 7 de febrero logran batir el record del mundo vigente y alcanzar la gloria con “Huaso” y Larraguibel.</p>

<p>En 1950 es ascendido a General de Brigada, comandando la División de Caballería con asiento en Santiago.</p>

<p>En 1952 se le concede retiro absoluto del Ejército pero siempre mantuvo el contacto y amistad con el mundo ecuestre y unidades de caballería y Carabineros. Por sus dotes de oficial y jinete fue condecorado en su carrera por Argentina, Bolivia, Paraguay, España, Portugal y México.</p>

