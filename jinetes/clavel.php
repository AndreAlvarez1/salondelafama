			<div class="bio col s12">

<p>Hijo mayor del Coronel de caballería Luis Clavel Dinatór, nació en la ciudad de Concepción el 17 de septiembre de 1920. Hizo sus estudios primarios y secundarios en los Padres Alemanes de Santiago.</p>

<p>Desde muy joven aprendió a montar con su padre en las diferentes unidades a las cuales sirvió, logrando una gran experiencia, que lo llevó a convertirse en el mejor exponente nacional en adiestramiento.</p>

<p>Incentivado por su padre ingresó a la Escuela Militar en el año 1935. En 1939 se gradúa como Alférez de caballería, siendo su primera unidad el regimiento N°7 Guías en Concepción, donde destacó por sus condiciones de instructor y excelente jinete.</p>

<p>En 1941 es destinado al Regimiento N°4 “Coraceros” en la ciudad de Viña del Mar, donde continuó practicando el deporte de su vida con pasión, destacando dentro de los oficiales de su regimiento.</p>

<p>Contrajo matrimonio con la señora Mary Matzen, con quien tuvo tres hijos: Luis Eduardo, Luis Héctor y Luis Enrique.</p>

<p>Su primer gran logro deportivo lo obtuvo en 1951 cuando integra el equipo chileno que ganó medalla de oro en los Juegos Panamericanos de Buenos Aires. Montando a la yegua “Frontarela”, además, obtuvo presea de plata en la competencia individual.</p>

<p>Un año después participa por Chile en los Juegos Olímpicos de Helsinki, Finlandia, quedando entre los 20 mejores jinetes del mundo y obteniendo el quinto lugar por equipo.</p>

<p>Ese mismo año participa en el Gran Premio de Hamburgo quedando en tercero y obteniendo además la “Cinta de Oro”, siendo hasta el día de hoy el único jinete nacional en obtener este logro.</p>

<p>En 1955 repite el oro en los Juegos Panamericanos. Esta vez el título lo consigue en la competencia individual junto a su yegua “Frontalera”. Ese año el Ejército le asigna la responsabilidad de ser Jefe del “Curso Maestro de Equitación”, impartiendo toda su experiencia y rigurosidad en todas las disciplinas a los nuevos jinetes de la institución y de oficiales de Ejércitos amigos invitados.</p>

<p>En 1956 asume como Jefe de la Escuela de Equitación, continuando la tarea anterior y efectuando múltiples modificaciones al plan de estudio de esta, debido a su gran experiencia en la docencia y gran jinete de Dressage.</p>

<p>En 1963, en Sao Paulo, obtiene un nuevo podio en los Juegos Panamericanos. Esta vez se colgó la medalla de bronce a lomos de “Chiporro”, un nuevo caballo que puso en la alta competencia y que fue trabajado por él mismo para este objetivo.</p>

<p>En 1969 es distinguido con el mando de la Dirección General de Fomento Equino y Remonta, donde hace un gran aporte en la crianza de caballos de deporte ecuestre en el Ejército y nacional, terminando esta fructífera gestión en el año 1970.</p>

<p>Una vez retirado de la Institución, en el año 1971, es contratado por el Club Deportivo Universidad Católica, como Maestro hasta el año 1974, siendo su paso un gran aporte para los jinetes de este club.</p>

<p>El Ejército de Chile en el año 1975 lo contrata nuevamente como maestro en la Escuela de Equitación, asumiendo las asignaturas de Asiento, Remonta y Alta Escuela. A su vez se desempeña como Jefe de la “Sección de Alta Escuela” hasta el año 1982.</p>

<p>Producto de una dolorosa enfermedad debe dejar de prestar servicio a la Escuela de Equitación del Ejército, pero su huella y enseñanzas impartidas en su larga trayectoria han sido imborrables.</p>

<p>Por los grandes logros deportivos obtenidos en su trayectoria, el Coronel Luis Clavel es indiscutiblemente el mejor jinete de adiestramiento que ha tenido Chile en esta disciplina al obtener cuatro medallas panamericanas.</p>

