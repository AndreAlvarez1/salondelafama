			<div class="bio col s12">

<p>Nació en Santiago en el 4 de abril de 1944. Sus padres fueron don Alberto Lavín Godoy y la señora Elba Hollub Gaspari. Ingresó a la Escuela Militar en 1959 y egresó de esta como Oficial de Caballería el año 1962.</p>

<p>Permaneció en la Escuela de Caballería de Quillota hasta marzo de 1974, siempre integrando el equipo representativo del instituto en todas las disciplinas. En ese año, junto con participar, se preparó e ingresó a la Academia Politécnica Militar.</p>

<p>Durante su permanencia en la Escuela de Caballería integró el Equipo de Salto de la Escuela y normalmente estuvo considerado en el Equipo de Salto Nacional.</p>

<p>Fue vicecampeón en el Campeonato Sudamericano Militar que se desarrolló en Uruguay, y en el que participaron además de los locales representativos de Perú, Argentina y Colombia.</p>

<p>En los Juegos Panamericanos de Cali, Colombia, en 1971, obtuvo junto a Bárbara Barone y los oficiales de Carabineros René Varas y Guido Larrondo, la medalla de bronce en salto por equipo. En estos Juegos, además, actuó como reserva de los equipos de adiestramiento y prueba completa.</p>

<p>Queda seleccionado y designado (junto a Barone, Varas y Larrondo) para integrar el equipo de salto nacional a los Juegos Olímpicos de Münich, Alemania, en 1972. Sin embargo, esta actuación no se pudo concretar debido a que el Comandante en Jefe, Carlos Prat no autorizó en aquel entonces a viajar al extranjero a los miembros del Ejército en las distintas disciplinas.</p>

<p>En sus actuaciones en el extranjero fue siempre Jefe de Equipo el eximio jinete, el Teniente Coronel Guillermo Aranda Parra. Otros maestros que influyeron en su vida de jinete fueron el General Hernán Castellón y el Teniente Coronel Antonio Piraíno.</p>

<p>Eugenio Lavín siempre mostró en su monta una gran elegancia y efectividad, lo cual se puede ver en sus fotos. Entre los años 2009 y 2013 volvió a su Escuela de Caballería, ejerciendo la docencia en el curso de Maestros de Equitación, ayudando a formar jinetes y caballos.</p>

<p>Actualmente reside en la ciudad de Osorno dedicado a la agricultura, siempre manteniendo contacto con la Federación Ecuestre de Chile y el mundo ecuestre.</p>

