			<div class="bio col s12">

<p>Nace el 16 de marzo de 1935, en Santiago. Sus padres son Arturo Escudero Otárola e Inés Troncoso Palacios.</p>

<p>Sus primeros pasos en la equitación los da en el Regimiento Maturana, unidad en la que prestaba servicios como instructor de equitación su hermano mayor, Alberto Labbé Troncoso, Teniente de Caballería en Comisión en dicha unidad.</p>

<p>Sus primeros maestros fueron el Capitán Iván Cáceres Larrañaga y el Teniente Manuel Rodríguez Véliz.</p>

<p>Desde el comienzo de su carrera practicó y participó en todas las disciplinas ecuestres, integrando los equipos de salto, adiestramiento, concurso completo de equitación y de polo de los regimientos “Coraceros” y “Cazadores”, e integró además el equipo de adiestramiento de la “Escuela de Caballería”.</p>

<p>Dentro de este periodo destacó con la medalla de oro que obtuvo en la competencia por equipos y plata en individual en la prueba de Adiestramiento en los Juegos Panamericanos de Winnipeg de 1967.</p>

<p>Otros logros destacados de este gran jinete fueron el título nacional que obtuvo en la categoría mediana y difícil en 1966 y los vicecampeonatos nacionales en 1965 y 1966 en las categorías principiante y fácil y mediana.</p>

<p>Fue integrante del equipo del regimiento “Coraceros” que se tituló campeón de Polo del Ejército, en enero de 1955; obtuvo también el segundo lugar en el Concurso Completo de Equitación organizado con motivo de la exposición de la Asociación de Industriales de Valparaíso y Aconcagua (ASIVA), en junio de 1957 y fue miembro del equipo del regimiento “Cazadores”, campeón del Ejército de polo en marzo de 1961.</p>

<p>En 1968 integró el equipo que obtuvo el sexto lugar en el Gran Premio de Adiestramiento de los Juegos Olímpicos de Ciudad de México.</p>

<p>Ese mismo año participó por última vez, antes de ser destinado desde la Escuela de Caballería a la Escuela de Suboficiales del Ejército, en el Concurso Hípico oficial organizado por la Escuela de Carabineros, obteniendo el primer lugar en el Gran Premio de Adiestramiento, con el caballo “Prete”. </p>

<p>En 1966 y 1967 fue profesor del curso de Maestros de la Equitación, realizado en la Escuela de Caballería. Entre 1988 y 1992 fue Maestro de Equitación del Club de Polo y Equitación San Cristóbal. </p>
	
<p>Durante 1981 y 1982, siendo Agregado Militar en Brasil, prepara ad-honorem al equipo de Prueba Completa de Brasil, en la especialidad de adiestramiento.
En este mismo periodo, además, prepara y presenta una sección de adiestramiento con los oficiales del Regimiento de Caballería “Dragones de la Independencia”, Unidad Escolta Presidencial, con motivo del aniversario de dicha unidad, en la ciudad de Brasilia.</p>


