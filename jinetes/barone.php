			<div class="bio col s12">

<p>Destacada amazona nacional que brilló en las pistas de nuestro país, América y Europa.</p>

<p>Nació en Venecia 1950, hija de Vitorio Barone y Laurita Guseo.</p>

<p>Vivió de niña en Buenos Aires, dando sus primeros pasos en la equitación, donde tuvo de maestro a Jorge Canaves y Jorge Villanueva. Arribó a Chile con su familia en 1959, teniendo como maestros, primeramente a Pelayo Izurieta y posteriormente en el club “El Relincho” a Mario Fuentes Fernández. En estos primeros años debutó en Chile y desde el principio demostró sus grandes condiciones.</p>

<p>Terminada su etapa inicial, comenzó a montar en el Santiago Paperchase Club de Avenida Ossa y posteriormente en el Club de su vida, Universidad Católica. Ya siendo una destacada amazona, la dirigió don Rafael Monti y el General Eduardo Yáñez. Este último le dio el impulso final para lograr los importantes triunfos internacionales que logró en su carrera deportiva.</p>

<p>Bárbara efectuó más de doce giras internacionales representando siempre en excelente forma nuestro país. Ha sido la única amazona nacional que logró una medalla panamericana, tras integrar el último equipo de salto nacional que alcanzó la medalla de bronce en Cali, Colombia, en 1972.</p>

<p>También participó en los Juegos Olímpicos de Munich, en 1972, obteniendo el mejor resultado de un equipo nacional en la disciplina de salto, después de la medalla de plata en Helsinki de 1952. En esta ocasión tuvo que saltar el caballo de capitán René Varas, el famoso “Quintral”, ya que estaba lesionado y ella era más liviana, pero igual de hábil en sus lomos.</p>

<p>Actualmente monta en forma recreacional y cría caballos en Argentina y el sur de Chile.</p>

<p>Esta distinguida amazona tuvo muchos caballos en su larga carrera ecuestre, pero recuerda con más cariño a los siguientes:</p>
<ul>
<li>-Yegua “Anaí”</li>
<li>-Yegua “Ventajita”</li>
<li>-Caballo “Negro”</li>
<li>-Caballo “Discutido”</li>
<li>-Yegua “Rumbita”</li>
<li>-Caballo “Altazor”</li>
</ul>

