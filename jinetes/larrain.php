			<div class="bio col s12">

<p>Nació el 11 de noviembre de 1926. Hijo de Joaquín Larraín Prieto y doña Sofía Gana Serrano. Aprendió a montar a caballo desde muy niño, en campo de su familia en Casablanca (V Región), gusto heredado por su padre quien fue oficial de caballería del Ejército.</p>

<p>Ingresó a la Escuela Militar en 1942, graduándose en 1946, egresando como alférez de caballería, siendo distinguido con el premio de mejor jinete de la promoción.</p>

<p>En 1947 fue destinado al regimiento de caballería N°1 “Granaderos”, en la ciudad de Iquique, donde tuvo el privilegio de tener grandes maestros entre ellos al capitán don Nibaldo Correa Fernández. En esta unidad de caballería sobresalió entre los oficiales jóvenes ganando prácticamente todos los concursos que se realizaron en la guarnición, haciéndose merecedor de que el comandante del regimiento lo proponga para ser destinado a la Escuela de Caballería en Quillota.</p>

<p>En 1948, ya en la Escuela de Caballería y sin ser Maestro de Equitación, participa en múltiples concursos ecuestres. Al año siguiente hace su debut internacional en el famoso concurso de Viña del Mar, ganando la prueba principal “Presidente Gabriel González Videla”. Su maestro fue el Capitán Horacio Ortíz quién fue jefe de equipo de Chile en reiteradas ocasiones.</p>

<p>En 1950 es destinado al regimiento Guías en Concepción, donde continua mejorando su condición de jinete y esta vez dirigido por el maestro Eugenio Barra Herrera, de quién aprendió muchos secretos de la equitación científica.</p>

<p>En Concepción, el capitán Werner Hertz, le presta uno de los caballos más famosos de Chile, el glorioso “Pillán”, con quien en el sur resultó imbatible. El éxito es tal, que el nuevo binomio no tuvo problemas para clasificar a los Juegos Panamericanos de Buenos Aires 1951.</p>

<p>En la capital argentina, Larraín obtuvo medalla de plata individual, en una actuación histórica que tuvo a tres nacionales completando el podio, luego que Alberto Larraguibel ganara el oro y Ricardo Echeverría el bronce. En la misma cita, Larraín obtuvo la presea dorada en la competencia por equipos.</p>

<p>Posteriormente, la Federación Ecuestre Argentina organizó un concurso Internacional poniendo en disputa dos pruebas principales: la primera “Evita Perón” y la segunda “Juan Domingo Perón”. Ante el asombro de los jinetes internacionales don Joaquín Larraín se adjudica la prueba “Evita Perón” en lomos del noble “Pillán”.</p>

<p>En 1952 es nominado para integrar la delegación que viajó a Europa a los Juegos Olímpicos de Helsinski. Antes de la cita de los cinco anillos, el equipo realizó una gira por Europa donde Larraín y “Pillán” destacan obteniendo el primer lugar en Soumur y Niza (Francia), el segundo puesto en Roma (Italia) y el quinto lugar en Hamburgo (Alemania). Joaquín Larraín no participó en las los Juegos Olímpicos de Helsinki por una decisión del jefe de equipo, la cual acató estoicamente, siendo su caballo “Pillán” entregado al capitán de Carabineros César Mendoza Durán.</p>

<p>En 1953 después de esta gira en el extranjero, es llamado a realizar el curso de Maestro de Equitación, del cual se gradúa en 1954. En 1955 ingresa al equipo titular del Ejército hasta el año 1957. En 1958 el Ejército le da la difícil misión de Fundar el criadero Militar “Las Bandurrias” en Coyhaique.</p>

<p>En 1960, después de una fructífera labor en la patagonia chilena, regresa al regimiento “Cazadores” en Santiago, participando activamente en todos los grandes premios y Concursos Internacionales realizados en Santiago y Viña del Mar.</p>

<p>En 1966 y junto a “Pillán” obtiene medalla de bronce individual y plata por equipos en los juegos Panamericanos de Chicago.</p>

<p>En 1968 siendo alumno regular de la Academia Militar del Ejército logra su último triunfo ecuestre importante, al coronarse como Campeón de Chile en todo competidor con su caballo “Reflejo”.</p>

<p>En su larga carrera deportiva inculcó a muchos jinetes militares y civiles el amor al caballo, el estudio y la práctica rigurosa de la equitación, legado que recibió de sus muchos maestros como don Eduardo Yáñez Zavala, don Guillermo Aranda Parra y don Alfonso Reyes.</p>

<p>Este gran jinete nacional ha traspasado toda su pasión por el deporte ecuestre y amor por el caballo a su hijo Joaquín Larraín Coddou, múltiple Campeón Nacional de Salto y destacado en el concierto Internacional, quién ha competido por nuestro país en Juegos Panamericano y Olímpicos.</p>

<p>Actualmente se encuentra retirado, pero siempre dando sabios consejos a jinetes amigos y concurriendo a los concursos más importantes realizados en Santiago.</p>
