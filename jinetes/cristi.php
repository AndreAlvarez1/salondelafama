			<div class="bio col s12">

<p>Este gran jinete nacional nació el 29 de junio 1916, en Valparaíso, hijo de Eduardo Cristi Prado y doña Rosa María Gallo Silva.</p>

<p>Lamentablemente perdió a su padre de niño, lo cual lo templó como hombre desde muy joven.</p>

<p>Sus primeros contactos con la equitación los recibió cuando realizaba su servicio militar como estudiante en el Regimiento de Caballería N°4 “Coraceros” en la ciudad de Viña del Mar. En este trance de juventud, demostró su amor a su patria y sus grandes condiciones de jinete, obteniendo la condecoración “18 de septiembre”, distinción que se otorga al mejor soldado de la promoción.</p>

<p>Posteriormente, el año 1935 ingresó a la Escuela de Carabineros de Chile como aspirante a oficial, destacando desde un primer momento entre sus compañeros.</p>

<p>Egresó de este Instituto en 1936 con el grado de Subteniente, siendo destinado a la 5° Comisaría en Aconcagua.</p>

<p>En el año 1937 es trasladado a la guarnición de Iquique donde contrae matrimonio con la Sra. María Marfil con quién tuvo cuatro hijos, entre ellos a la destacada amazona y diputada nacional María Angélica Cristi.</p>

<p>En 1938 estando en la nortina ciudad destacó por sus dotes de jinete en las competencias que se hacían habitualmente entre las unidades de caballería y policiales, obteniendo en reiteradas ocasiones los primeros lugares.</p>

<p>En 1940 por sus dotes de distinguido Oficial y gran jinete es destinado a la Escuela de Carabineros en Santiago, donde practicó con mucho entusiasmo la equitación y también formando muchos caballos.</p>

<p>En 1942 es trasladado a la guarnición de Valparaíso donde siguió practicando la equitación y logra su primer triunfo internacional en los concursos de verano en la ciudad de Viña del Mar.</p>

<p>En 1943 integra el primer curso de equitación de Carabineros de Chile, donde recibe enseñanzas principalmente del maestro Amaro Pérez de Castro, maestro formado en Europa, en la Escuela de Equitación de Hannover (Alemania)</p>

<p>En 1948 es comandado a estudiar a la Escuela de Policía Militar en la ciudad de Pensilvania en Estados Unidos y ese mismo año es nominado para integrar el equipo de salto de Carabineros de Chile que efectuó una gira por el país del norte con gran éxito en la ciudad de Harrysburg y Nueva York.</p>

<p>En 1949 es nominado para integrar el equipo nacional que participa en Canadá y Estados Unidos logrando con su caballo “Scarface” el primer lugar en el Gran Premio en el Madison Square Garden (Premio West Point) en la ciudad de Nueva York, triunfo que, tanto para él cómo su familia, es el más preciado después de las medallas olímpicas de Helsinki, ya que le arrebató la victoria al destacado jinete mexicano Humberto Mariles.</p>

<p>En 1951 es seleccionado por la Federación Ecuestre para integrar el equipo nacional de salto que participaría en los Juegos Olímpicos de Helsinski, realizando una gran gira preparatoria por Italia, España, Alemania y Francia, obteniendo en este último país triunfos con sus caballos “Scarface”, “Soumur” y "Bambi". También empató el primer lugar en Alemania con el Capitán de Ejército José Echeverría.</p>

<p>En 1952, en los Juegos Olímpicos de Helsinski, logra junto al equipo nacional la medalla de plata a pesar del infortunado viaje en barco de Buenos Aires a Europa donde los caballos de estos jinetes llegaron en su totalidad heridos y pierde la vida en cubierta el caballo campeón panamericano "Julepe". Esta lamentable situación provocó un gran inconveniente para el jefe de equipo, el General Eduardo Yáñez Zavala, quién tomó la resolución de desmontar a algunos jinetes y reasignar caballos en beneficio del Equipo Nacional.</p>

<p>En su caballo “Bambi”, logra además la medalla de Plata individual, luego de realizar en un desempate con cuatro jinetes de primera línea, siendo el único jinete nacional en tener dos medallas olímpicas y dos panamericanas.</p>

<p>En 1954 ingresa al Instituto Superior de Carabineros, sin dejar de practicar su deporte y cooperar al desarrollo de la equitación nacional e institucional, hecho que lo distinguió en su paso por Carabineros de Chile.</p>

<p>En 1955 es nominado para integrar el equipo de Chile que participaría en los Juegos Panamericanos en la ciudad de México, obteniendo la medalla de Bronce por equipos. Ese mismo año es ascendido al grado de Mayor.</p>

<p>En el año 1959 por su gran experiencia es nominado nuevamente para integrar el Equipo Nacional y representar a nuestro país en la ciudad de Chicago, Estados Unidos, dando un nuevo triunfo a la equitación nacional, logrando la medalla de bronce por Equipos.</p>

<p>En 1960 por sus méritos profesionales es ascendido al grado de Teniente Coronel y nombrado Subdirector de la Escuela de Carabineros, dando un fuerte impulso a todas las actividades para formar a los Oficiales de esta Institución y en especial fortaleció su equipo ecuestre.</p>

<p>En 1962 asciende al grado de Coronel, siendo comisionado a Estados Unidos para realizar un curso de Administración y Alto Mando.</p>

<p>En 1964 asciende al grado de General de esta Institución Policial y se desempeña como Director del Personal.</p>

<p>En su carrera profesional siempre demostró preocupación por su gente y difundió la práctica de la equitación como una forma de vida, logrando continuar con el desarrollo de nuevos jinetes institucionales.</p>

<p>En 1965 es nombrado como General Sub Director de Carabineros de Chile, ese mismo año fallece en un trágico accidente de tránsito a la altura de San Fernando, lo cual enlutó a esta institución y a la equitación nacional. A sus funerales asistieron los Presidentes Eduardo Frei, en ejercicio, y Jorge Alessandri, quienes le tenían un gran afecto por su condición de caballero y deportista.</p>

<p>Este gran jinete fue vicepresidente de la Federación Ecuestre de Chile. En esta labor demostró gran personalidad y criterio para relacionarse con todo tipo de personas, desde ordenanzas, hasta presidentes y nobleza que practica este deporte. También en Carabineros de Chile fue promotor de la recordada institución “Niño y Patria” y del Escalafón Femenino.</p>

<p>Actualmente la Escuela de Caballería de Carabineros, enclavada en la comuna de Maipú, lleva su nombre debido al importante aporte que este gran jinete y caballero realizó por la equitación en esta institución.</p>

