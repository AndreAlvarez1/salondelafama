			<div class="bio col s12">

<p>Nace en la ciudad de Antofagasta el 31 de mayo de 1918. Hijo de Don Aníbal Echeverría Bari y Doña Inés Vergara Baeza. Era el menor de dos hijos de la familia. Debido a la temprana muerte de su hermano mayor, prácticamente se crió como hijo único.</p>

<p>Su tío abuelo, el Teniente Coronel José Echeverría Reyes fue Comandante del Glorioso Batallón “Bulnes”, de heroica participación en el Asalto y Toma del Morro de Arica durante la Guerra del Pacífico en 1879.</p>

<p>Su Padre, Don Aníbal Echeverría Bari, debido a su cariño por el Ejército, fue Oficial de Reserva del Glorioso Regimiento de Caballería “Cazadores”, alcanzando el grado de Capitán.</p>

<p>Hizo sus primeros estudios de enseñanza básica en el Colegio San Luís de Antofagasta. Por razones de trabajo de su padre se trasladan a vivir a Santiago, donde continúa sus estudios en el Liceo Alemán.</p>

<p>Desde pequeño, demuestra un gran interés y cariño por los animales, especialmente los caballos. Desde los cinco años salía a montar con su padre a recorrer el campo familiar que tenía en la ciudad de Bulnes. Tuvo de regalo su primer caballo a los seis años. Todas sus vacaciones y feriados partía al campo para cabalgar y cuidar su caballo.</p>

<p>Ingresó a la Escuela Militar en 1935 y egresa como Alférez de Caballería en 1938, con la primera antigüedad del arma.</p>

<p>Su primera destinación fue en el Regimiento de Caballería “Cazadores” en Santiago y en 1940 es destinado a la Escuela de Caballería en Quillota. Siempre ocupó el primer lugar en los Cursos de Tenientes, Curso de Maestros de Equitación y de Capitanes. Posteriormente ingresa a la Academia de Guerra del Ejército y siendo alumno de tercer año pide su renuncia voluntaria de la Institución, con 22 años de servicio.</p>

<p>Sus principales y más importantes triunfos, logros y cargos ecuestres, en orden cronológico, son los siguientes:</p>

<p>1938 – 39 – 40 Campeón del Cross Country Nacional (Carrera de Obstáculos para Jinetes de Equitación), corridas en el Club Hípico de Santiago. (Yegua “Babilonia”)</p>

<p>1938 – 39 – 40 Integrante del Equipo de Polo del Ejército, ganador de los Campeonatos Nacionales en 1939 y 1940.</p>

<p>1945 Realiza en Curso de Maestros de Equitación, obteniendo el 1º lugar de este.</p>

<p>1946 a 1952 Integra el Equipo de Chile de Saltos.</p>

<p>1947	Bate el Récord Mundial de la Prueba “A la Americana” con 70 saltos (52 el anterior). Esta competencia se realizó en la Quinta Normal (Yegua “Morenucha”)</p>

<p>1948 Como Integrante del Equipo de Chile en el Concurso Hípico Internacional de Viña del Mar bate el récord mundial de la “Copa de Naciones” (puntos y tiempo) y además, obtiene el 1º lugar individual. (Caballo “Sileno”)</p>

<p>1949 Ganador del Gran Premio Colombia en el Concurso Hípico Internacional de Viña del Mar. (Caballo “Sileno”).</p>

<p>1949 Integrante del Equipo de Chile que obtiene el 1º lugar en la Copa de Naciones en el C.H.I. del Madison Square Garden, EE.UU. (Yegua “Cueca”).</p>

<p>1949 Obtiene el primer lugar individual en el “Campeonato de Invierno” del Concurso Hípico Internacional de Toronto, Canadá. (Caballo “Sileno”).</p>

<p>1950 Integrante del equipo ganador de la “Copa de Naciones”, en el Concurso Hípico Internacional Sausalito, en Viña del Mar. (Caballo “Sileno”)</p>

<p>1951 Integrante del Equipo de Salto campeón de los Juegos Panamericanos de Buenos Aires, Argentina y vicecampeón individual. (Caballo “Bambi”)</p>

<p>1952 Ganador del Gran Premio en el prestigioso Concurso Hípico Internacional Piazza di Sena, que se disputa en Roma, Italia. (Caballo Pillán). Este importante triunfo le valió recibir como premio una motoneta “Vespa”, vehículo muy cotizado en esos años.</p>

<p>Integrante del Equipo Vicecampeón de Salto en los Juegos Olímpicos de Helsinki, Finlandia (Caballo “Lindo Pial”)</p>

<p>Elegido como el Mejor Deportista en Equitación, del Círculo de Periodistas Deportivos de Chile.</p>

<p>1953	Jefe de Equipo de Chile en la Gira por EE.UU y Europa.</p>

<p>1954 Estando en el tercer año de la Academia de Guerra del Ejército se retira del Ejército con el grado de Mayor.</p>

<p>1955 a 1960 Maestro de Equitación de la Universidad Católica.</p>

<p>1961 a 1975 Director de la Federación Ecuestre de Chile.</p>

<p>1975 Jefe de Equipo de Chile Juvenil, que obtiene el Campeonato Sudamericano en Montevideo, Uruguay.</p>

<p>1976 a 1983 Maestro de Equitación del Club de Polo y Equitación San Cristóbal.</p>

<p>1984 a 1987 Como asesor de la Federación Deportiva Militar FEDEM.</p>

<p>1987 Recibe el “Lautaro de Oro”, trofeo que entrega la Confederación Deportiva de las Fuerzas Armadas a los Deportistas destacados de las Fuerzas Armadas.</p>

<p>Durante 30 años trabajó en el “Sociedad Hipódromo Chile”, desempeñándose como Comisario.</p>

<p>Una de sus más divertidas anécdotas fue en 1952, regresando de las Olimpiadas de Helsinki, el equipo chileno pasó a participar en el concurso internacional del Centenario de Río de Janeiro, Brasil. La mayoría de los caballos chilenos tenían nombres autóctonos: “Pillán”, “Maitén”, “Cueca”. Al hacer las inscripciones y él montando a “Cueca”, las organizaciones del concurso le dicen que deben cambiar el nombre de la yegua, ya que, en ese país, el significado de ese nombre era “calzoncillos” y no podían anunciar al Capitán Echeverría montando en “calzoncillos”. A raíz de lo anterior, esa yegua cambió su nombre por “Tieng – Sieng”.</p>

<p>Es el único militar (a la fecha) con medalla olímpica. Siempre se caracterizó por ser una persona muy amable, sencilla, conversadora, pero muy humilde en relación a sus logros ecuestres, tanto así que la revista deportiva Estadio, le dedicó diferentes reportajes, destacando en sus titulares: ”La Mejor Carta Chilena”, “El Olímpico Olvidado”. Revistas Europeas especializadas (Anne Hippique), después de la gira y olimpiadas dijeron de nuestro jinete nacional: “Tuvimos el placer de observar a uno de los mejores jinetes que han paseado sus triunfos en canchas europeas, el Capitán Echeverría de Chile, jinete de gran efectividad y depurado estilo”.</p>

<p>Falleció en Angol el 15 de agosto de 1988, a los 70 años de edad. Casado con la Sra. María Margarita Egaña Cruz, con quien tiene nueve hijos (siete mujeres y dos hombres).</p>

