			<div class="bio col s12">

<p>Nace en Santiago el 21 de febrero del año 1928, hijo de don José Mela.</p>

<p>Ingresó a la Escuela Militar en año 1945, pese a la negativa de su progenitor, pero fue más el amor a su patria y al Ejército.</p>

<p>Se gradúa en diciembre de 1946 como Alférez de Caballería.</p>

<p>En 1947 es destinado a su primera unidad Regimiento de Caballería N°7 Guías de Concepción, donde tuvo el privilegio de tener como maestro al gran jinete don Mario Leumberg Carvajal quién le inculcó el amor por el caballo y como llegar a su corazón para lograr lo mejor de él.</p>

<p>En el año 1948 fue destinado al Regimiento Cazadores en Santiago, donde integra el equipo de salto de ese Regimiento. A fines de este año sufrió un grave accidente por aplastamiento de un caballo en instrucción, lo cual le dificultó seguir participando regularmente.</p>

<p>En 1950 fue destinado al regimiento Húsares en Angol, donde se le recrudeció una lesión al hombro sufrida años anteriores, teniendo que ser operado de urgencia y demorándose casi un año en recuperar su capacidad física.</p>

<p>En 1952 contrae matrimonio con la Sra. Raquel Cañas Valverde, con quien tiene cuatro hijos.</p>

<p>En 1952 vuelve al Regimiento Cazadores en Santiago retomando plenamente su actividad como jinete logrando grandes triunfos en categorías mayores.</p>

<p>En 1953 es destinado a la Escuela de Caballería en Quillota, para hacer el curso de Teniente.</p>

<p>En 1954 realiza el curso de Maestro de Equitación, graduándose con el primer puesto, reconociendo que tuvo un gran maestro que le enseñó las primeras armas y secretos en el Adiestramiento don Guillermo Aranda Parra.</p>

<p>En el año 1957 debido a que existían muchos y muy buenos jinetes de salto le ordenaron que a partir de ese año se debía dedicar al adiestramiento lo cual sin lugar a dudas fue un duro golpe a sus pretensiones de tener una brillante carrera deportiva y a pesar de su desmotivación inicial, se impuso la meta de lograr ser el mejor en esta disciplina poniéndose a trabajar.</p>

<p>En 1958 se coronó Campeón de Chile en la categoría Difícil en el caballo “Vía Reggio”, ante la sorpresa del mundo ecuestre, ganándole a sus maestros y a los mejores jinetes de América.</p>

<p>Al año siguiente nuevamente se coronó Campeón Nacional en la misma categoría con el caballo “Materhorn”. Por estos triunfos fue nominado por la FECH como integrante del Equipo Nacional que participó en los Juegos Panamericanos de Chicago en 1959, logrando medalla de oro por equipo y medalla de plata individual.</p>

<p>En 1960 después de haber obtenido la clasificación y preparándose para participar en los Juegos Olímpicos de Roma, al llegar a Buenos Aires fue suspendida la gira por el gran terremoto ocurrido en Chile ese año. La FEI notificó a nuestro país que cancelarían todos los gastos de participación del Capitán José Mela y del Mayor César Mendoza para participar en este magno evento como una manera de solidarizar con nuestro país pero lamentablemente el alto mando institucional no autorizó esta participación.</p>

<p>Posteriormente ingresa a la Academia de Guerra del Ejército por tres años, alejándose de la competencia y dedicándose a su carrera profesional. Una vez graduado con el título de Oficial de Estado Mayor, trabajó en diferentes estamentos institucionales de gran responsabilidad.</p>

<p>En 1971 es nombrado Edecán del Presidente de la República y en 1973 es destinado a la Misión Militar en España.</p>

<p>En 1974 se retira de la Institución con 30 años de servicio.</p>

<p>Después de su retiro del Ejército, se dedicó a trabajar y asesorar en la empresa privada en el rubro de la construcción.</p>

<p>También mantuvo el contacto permanentemente con su Ejército, a través del regimiento de Caballería tradicional “Bueras”, integrando su directiva y ayudando a sus iniciativas solidarias para ayudar a los compañeros caídos en desgracia y con mala salud.</p>


