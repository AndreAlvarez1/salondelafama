			<div class="bio col s12">
		<p>Como deportista tuvo destacados triunfos en la equitación, siendo el más recordado el récord mundial de salto alto que batió en Viña del Mar en 1949 junto con su caballo “Huaso”, al saltar 2,47 metros. Después de muchos intentos, este récord aún no ha sido batido.</p>

<h2>Biografía</h2>

<p>Alberto Larraguibel Morales fue hijo de Alberto Larraguibel y de Filomena Morales. Vivió cerca de la Pampa de Granaderos, un campo donde los militares del Regimiento Húsares entrenaban con sus unidades montadas y también en forma deportiva, y realizó sus estudios de preparatoria y humanidades en el Seminario de Concepción y en el Liceo de Angol.</p>

<p>El 1 de enero de 1938 ingresó a la Escuela Militar del Libertador Bernardo O'Higgins y dos años más tarde egresó con el grado de alférez del arma de caballería. Fue destinado al Regimiento Húsares de Angol, donde comenzó a destacar en los concursos de equitación y llegó a ser parte del equipo ecuestre del regimiento. Obtuvo resultados que lo llevaron a ser nombrado alumno del curso extraordinario de equitación en la Escuela de Caballería de Quillota en 1946.</p>

<p>El 16 de enero de 1947 se tituló como Maestro de Equitación. Con este título, representó a Chile en múltiples competencias nacionales e internacionales, con destacadas participaciones. Posteriormente, sirvió en diversas unidades militares del Ejército, como el Regimiento Cazadores, la Escuela de Caballería, el Regimiento Coraceros, la Academia de Guerra, el Depósito Central de Remontas, el Regimiento Guías, el Regimiento Húsares, la Comandancia del Haras Nacional, la Dirección de Fomento Equino y Remonta y la Dirección de Logística.</p>

<p>En 1966 fue nombrado comandante del Regimiento Húsares, unidad que marcó un hito en su trayectoria militar. El 1 de agosto de 1973, pasó a retiro; sin embargo, el 17 de septiembre de 1976 fue llamado al servicio activo del Ejército. Se retiró definitivamente de la institución en 1979, después de haber permanecido en el Ejército por más de 38 años.</p>


<h2>Trayectoria deportiva</h2>

<p>Desde el inicio de su carrera deportiva Larraguibel tuvo destacadas participaciones en competencias nacionales e internacionales. Durante su carrera, integró el equipo de salto de los Regimientos Húsares, Cazadores y Coraceros y de la Escuela de Caballería; asimismo, participó en concursos ecuestres internacionales en Europa y América del Sur, tales como los efectuados en Roma, Madrid, Vichy, Lucerna y Río de Janeiro, entre otros.</p>

<p>En 1947 recibió el caballo “Faithful”, que venía de los hipódromos y no había tenido buenos resultados, al cual rebautizó posteriormente como “Huaso”. Larraguibel lo entrenó en salto alto, obteniendo importantes logros a nivel sudamericano, como el récord de salto ecuestre que batió en 1948 con 2,37 metros bajo la dirección del Coronel Rafael Monti.</p>

<p>También integró el equipo representativo de Chile en los Juegos Panamericanos de 1951 en Buenos Aires, donde Larraguibel se adjudicó dos medallas de oro: una individual en la Copa de Naciones montando a “Julepe” y otra por equipos junto a Ricardo Echeverría, Joaquín Larraín y César Mendoza.</p>

<p>Posteriormente, se preparó para disputar los Juegos Olímpicos de Helsinki 1952. Fue a una gira a Europa donde obtuvo buenos resultados; sin embargo, no pudo integrar el equipo de salto en las Olimpiadas debido a un accidente durante su gira europea, quedando lesionado. Sin embargo, Larraguibel es conocido en Chile y el ámbito internacional, por ser el único deportista que ha alcanzado un récord mundial para dicho país.</p>

<p>En el Regimiento Coraceros de Viña del Mar el 5 de febrero de 1949, el entonces Capitán Larraguibel junto con su caballo “Huaso” lograron superar los 2,47 metros de altura a las 17:59 horas. Con esta marca, batió el récord anterior de 2,44 metros, en manos del jinete italiano Antonio Gutiérrez, en Osoppo, establecido en Roma el 27 de octubre de 1938. Asistieron cinco mil personas, entre las que se contaban el Presidente de la República Gabriel González Videla, seis jueces internacionales y la tripulación del buque escuela francés Jeanne d'Arc. Esta prueba estaba inserta en el programa del Concurso Hípico Internacional con la participación de los equipos de salto de Bolivia, Chile y Colombia.</p>

<p>La Escuela de Equitación del Ejército de Chile, ubicada en la ciudad de Quillota, lleva su nombre y en ella se encuentra una réplica del histórico salto de 1949. El 14 de diciembre de 2007 se inauguró el monumento «Capitán de Ejército Alberto Larraguibel», que se encuentra ubicado en la Avenida Jorge Montt, en el paseo costero de Viña del Mar, justo frente donde antiguamente se encontraba el Regimiento de Caballería "Coraceros". El trabajo fue obra de Francisco Torres y lo realizó en Quilpué. La figura es de bronce, mide cinco metros de altura y pesa casi cinco toneladas.</p>

