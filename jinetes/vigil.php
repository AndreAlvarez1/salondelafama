			<div class="bio col s12">

<p>Nace el 29 de Julio de 1910 en la ciudad de Viña del Mar. Ingresa a la Escuela Militar como Cadete en 1927 y se gradúa como Oficial de Ejército en 1929, con el grado de Alférez en el arma de caballería. En este instituto tiene sus primeras instrucciones formales de equitación por maestros alemanes y chilenos titulados en ese país.</p>

<p>En 1932 es destinado al Regimiento de caballería “Coraceros “, después de estar tres años en la lejana provincia de Magallanes. En 1934 es destinado a la Escuela de Caballería por sus dotes de jinete, al curso de Maestros de Equitación, del cual se gradúa un año más tarde.</p>

<p>En 1935 contrae matrimonio con la señorita María Beatríz Pepper Santa Cruz.</p>

<p>En 1939, asciende al grado de Capitán y es trasladado al Regimiento de Caballería “Cazadores a Caballo”, donde participa en todas las disciplinas y difunde la equitación a civiles y carabineros, con clásica rigurosidad y perfeccionismo. Ese mismo año realiza una segunda gira con el equipo de salto nacional a Estados Unidos, al mando del Coronel Eduardo Yáñez.</p>

<p>En 1949 regresa a la Escuela de Caballería como jefe de estudios y retoma activamente la práctica de la equitación y docencia, destacando en el Concurso Completo de Equitación.</p>

<p>En 1950 es nominado para integrar el equipo nacional de salto que realiza una gira a Europa. Un año más tarde y en la especialidad de prueba completa es nominado como integrante del equipo nacional para participar en los Juegos Panamericanos de Buenos Aires, donde obtiene medalla de plata en la competencia individual.</p>

<p>En 1952 es nominado al equipo nacional de concurso completo para participar en los Juegos Olímpicos de Helsinski. Su actuación, sin embargo, no se pudo concretar debido a una lesión que sufrió su caballo mientras participaba en el cross country y tuvo que abandonar la competencia.</p>

<p>En 1956 es ascendido al grado de Teniente Coronel y nombrado Comandante del Regimiento Coraceros de Viña del Mar, donde destaca su capacidad organizativa y apoyo a la FECH, en la realización de los concursos internacionales de verano y se difunde la equitación en esa hermosa ciudad.</p>

<p>En 1958 realiza un curso de perfeccionamiento de Estado Mayor por un año en EE.UU., en Fort Holabird, Maryland. Un año más tarde regresa a Chile y es ascendido a Coronel y destinado a Santiago como Jefe de Estado Mayor a la división de Caballería, participando activamente como dirigente en la Federación Ecuestre de Chile, sin dejar de participar como jinete.</p>

<p>En 1961 es elegido como Presidente de la FECH, cargo que ostenta por dos períodos, dirigiendo en excelente forma los destinos de la equitación nacional.</p>

<p>En 1962 es nombrado Director de la Escuela de Caballería, en la ciudad de Quillota, dando un fuerte impulso a los equipos representativos de la Institución en todas las disciplinas, sin dejar de ser Presidente de la FECH.</p>

<p>En 1963 es nombrado Jefe del Equipo de Chile, con el fin de preparar a los binomios y para asistir a los Juegos Olímpicos de Tokio, donde también es nominado por la FEI “Presidente del gran Jurado”, en la disciplina de Concurso Completo de Equitación, siendo hasta el momento el único chileno en tener tan alta distinción.</p>

<p>En 1965 se retira de Ejército con 33 años de servicio efectivos en la institución. En 1966, en situación de retiro y por su vasta experiencia como jinete y dirigente, continúa asesorando al Ejército en los aspectos de instrucción de equitación y deportiva, hasta el año 1977.</p>

<p>Cooperando a la organización y desarrollo de la Federación Deportiva Militar (FEDEM), para mejorar el deporte en las FFAA y participa en el directorio de la FECH.</p>

<p>En los últimos años de su vida, aquejado por una larga y penosa enfermedad, se daba el tiempo para ir a ver los concursos hípicos a Santiago y Viña del Mar, en silla de ruedas ayudado por su señora, dando consejos y recomendaciones a los jóvenes jinetes participantes, siempre distinguido y elegante.</p>


