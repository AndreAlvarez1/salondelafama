			<div class="bio col s12">

<p>Nació en Mulchén , Provincia de Bio.Bio VII Región de Chile.</p>

<p>Realizó sus estudios primarios en  el British High School en Santiago.</p>

<p>Los estudios secundarios los realizó en el Colegio San Pedro Nolasco y en la Escuela Militar instituto castrense al que ingresa el año 1945.
También estudio Leyes (Derecho) en Chile y en Colombia.</p>

<p>Egresa de  la ESCUELA MILITAR,  GRAL BDO O´ HIGGINS  el año 1949, como oficial de Ejército del arma de Caballería.</p>

<p>Realiza el curso de “MAESTRO DE EQUITACIÓN” EN 1954, obteniendo el primer lugar en el Ejército Argentino (CAMPO DE MAYO), al año siguiente hace el mismo curso en la  Escuela de Caballería en la Ciudad de Quillota.</p>

<p>Tras su retiro del Ejército, trabaja en MADECO como director de personal, sin dejar de participar y hacer clases a diferentes alumnos.
Realiza cursos de perfeccionamiento de administración de personal en Turín – Italia el año 1969.
Ese mismo año estando en Europa, se perfeccionó en la Escuela Española de Viena – Austria, en el trabajo con “cuerdas largas”, lo cual le permitió traspasar este importante conocimiento a sus alumnos en años posteriores, dedicado a la docencia, especialmente en la Escuela de Equitación del Ejército.</p>

<p>En su carrera  deportiva obtuvo los siguientes logros:</p>

<p>1953 medalla de oro en CCE  por equipos en el Campeonato (Quillota).</p>
<p>Ganador del Ranking  en la disciplina de salto año 1956.</p>
<p>Campeón Nacional de Salto  en el campeonato del mismo año.</p>
<p>Entre los años 1960 al 1967, campeón nacional en diferentes categorías de caballos en la disciplina de adiestramiento.</p>
<p>1968 Campeón Nacional en categoría difícil y nominado para el equipo nacional que participó en las Olimpiadas de México.</p>
<p>1971 Es nominado para integrar el equipo Nacional de la disciplina de adiestramiento, que participaría en Cali –Colombia, obteniendo la medalla de Plata por equipos,  siendo esta la última presea de Chile en esta disciplina.</p>

<p>Desde los años 1972 al 1977, se radicó en Colombia, realizando una fructífera labor como maestro de equitación, siguiendo la huella de otros grandes maestros chilenos en el extranjero.</p>

<p>De regreso a nuestro país y trabajando nuevamente como maestro de equitación en el Ejercito, es nombrado Jefe del equipo de Chile de CCE, que participó en el sudamericano en Buenos Aires- Argentina, obteniendo el Vice campeonato.</p>

<p>El año 1981, se traslada a Buenos Aires y ejerce como maestro en el club Hípico Argentino, hasta el año 1985.</p>

<p>Nuevamente de regreso a su país,  es contratado por  como profesor y asesor en la Escuela de Equitación del Ejército y Carabineros.</p>

<p>1985 Se desempeña como jefe de equipo de CCE, en los JJPP de Indianapolis, obteniendo medalla de bronce con sus dirigidos, siendo esta la última medalla de la Equitación Nacional.</p>

<p>En el año 1998, es nombrado jefe del equipo de CCE , para participar en el Campeonato sudamericano efectuado en el centro Ecuestre Santa Sara – Santiago, logrando el primer lugar por equipo y 1° y 2° lugar en individual.</p>

<p>También ha dirigido equipos de Venezuela y Colombia , en las disciplinas de Adiestramiento y CCE.</p>

<p>En su larga y fructífera vida de maestro y docente, también ha ejercido estas funciones en diferentes clubes civiles, como en el Club Universidad Católica  y Club Ecuestre Internacional, en el cual una de las pistas lleva su nombre.</p>

<p>Actualmente, continúa haciendo clases y asesorando a jinetes y maestros, en el desarrollo de caballos y alumnos. Su huella en el Ejercito de Chile, Carabineros y clubes civiles , ha sido imborrable , con sus sabios consejos y ejemplo de probidad, caballerosidad, trabajo y amor el caballo. </p>



