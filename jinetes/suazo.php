			<div class="bio col s12">
		<p>Nace el 1 de enero de 1957. Sus padres son Víctor Manuel Rodríguez y Violeta Suazo Faundez. </p>

<p>Vivió su infancia y juventud en la ciudad de Cauquenes, donde vivió al lado de los caballos merced a que siempre acompañó a su padre en las labores que demandaba el fundo “Curica”. </p>

<p>Desde muy joven  se dedicó a la práctica del judo, deporte en el que obtuvo cinturón negro a los 19 años y luego se consagró campeón nacional.</p>

<p>Su acercamiento a la equitación se produjo a instancias del Capitán de Carabineros Alejandro Carrasco, quien tras comprobar que era sobrino del destacado equitador Héctor Rodríguez lo invita a practicar este deporte.</p>

<p>Sus primeros maestros de equitación fueron Rafael Montti y Ramón Urzúa. </p>

<p>En su destacada trayectoria deportiva brilla la medalla de bronce en la competencia por equipos en la especialidad de Concurso Completo en los Juegos Panamericanos de Indianápolis 1987. Este logro lo hace merecedor de la distinción del Círculo de Periodistas Deportivos de Chile, como el mejor atleta de la equitación de la temporada 1988.</p>

<p>En la misma competencia continental, obtiene en 1995, en Mar del Plata, el quinto lugar; repitiendo la misma posición que alcanzó en año anterior en el Campeonato Sudamericano disputado en Argentina. En Bogotá, obtiene en 1984, la medalla de oro en el Campeonato Latinoamericano de Policías Uniformadas, montando a la yegua “Rambla”</p>

<p>A lomos de “Neyén”, obtiene en 1992 el título sudamericano. </p>

<p>En el plano local destaca con los títulos nacionales de las temporadas 1992, con “Neyén” y 1998 en la categoría intermedia con “Lancelot”.</p>

<p>Otros logros destacados son el tercer lugar que obtuvo en la Semana Hípica disputada en Montevideo, Uruguay, en 1989 y los vicecampeonatos nacionales de 1981 y 1986.</p>

<p>En su rol como Maestro de Equitación destaca con su trabajo como profesor del Club Ecuestre Escuela de Suboficiales de Carabineros durante 1980 y 1983; profesor del curso de Instructores y Maestros de Equitación del Club Ecuestre Escuela de Carabineros de las promociones 1984 a 1991; profesor del Club Ecuestre Prefectura de Osorno entre 1992 y 1993; profesor del Club Ecuestre Prefectura de Carabineros de Temuco entre 1996 y 2001 y profesor del Club Frontera Country Club Temuco entre 2002 y 2007.</p>

