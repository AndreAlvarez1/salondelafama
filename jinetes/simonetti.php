			<div class="bio col s12">

<p>Destacado jinete nacional que brilló en las pistas de nuestro país, América y Europa, siendo todo un hito en el deporte nacional.</p>

<p>Nació en Santiago el año 1945, hijo de Américo Simonetti Fiorentinni y Ana Mazzarelli Morra. Vivió en Santiago con su familia y aprendió a montar en caballos criollos en una propiedad familiar en la localidad de El Monte.</p>

<p>Recibió sus primeras clases de equitación en el Santiago Paperchase Club, cuando estaba ubicado en Avenida Ossa, donde tuvo de maestro al Mayor Luis Larenas y al 
General Francisco Lira.</p>

<p>En 1959 se cambió al club Universidad Católica, institución deportiva que representó casi toda su vida, donde tuvo como maestros a los Coroneles Héctor Clavel y Rafael Monti, quienes influyeron en su desarrollo como jinete y sus éxitos nacionales e internacionales.</p>

<p>Este gran jinete efectuó más de 15 giras internacionales, representando siempre en excelente forma nuestro país, destacando en Panamericanos, Juegos Olímpicos y grandes premios en Europa.</p>

<p>Según el propio equitador, su triunfo más recordado es el Gran Premio de Rotterdamm, donde obtuvo el 2° lugar entre los mejores jinetes europeos y del mundo.</p>

<img src="../images/simonetti/farfala.png" alt="farfala" width="100%" style="margin-bottom: 10px">

<p>Hace poco tiempo y con más de 70 años, continuaba participando con mucho éxito en grandes premios en Chile y Argentina con “Fe Grande” y “Farfala”.</p>

<p>Actualmente no participa en forma competitiva, pero si trabaja caballos nuevos que él mismo cría. También dirige jinetes jóvenes que participan en competencias nacionales montando sus caballos.</p>

<h2>CABALLOS DESTACADOS</h2>
