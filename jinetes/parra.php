			<div class="bio col s12">

<p>Nace el 28 de octubre de 1921 en la localidad de San Carlos, en Chillán. Su vida transcurre entre el colegio y el campo de sus padres llamado Vista Hermosa, donde toma contacto con los primeros caballos y aprende a montarlos y amarlos.</p>

<p>En 1941 hace el Servicio Militar como Aspirante a Oficial de Reserva en el Regimiento de Caballería N°4 “ Coraceros” en la ciudad de Viña del Mar. Dos años más tarde es destinado al Regimiento de Caballería N°1 “Granaderos“ en Iquique, integrándose al equipo ecuestre de la unidad, ya demostrando grandes condiciones como jinete e instructor de caballería.</p>

<p>En 1951 obtiene el Título de “Maestro de Equitación”, destacando como el mejor alumno de su promoción y quedando como titular del equipo ecuestre del Instituto, también dedicado a la docencia.</p>

<p>Su primer gran logro internacional lo obtiene en 1955 cuando integra el equipo nacional que obtuvo medalla de bronce la Copa Naciones que se disputó en México. Mario Lueyemberg, Leopoldo Cover, Guillermo Aranda y Óscar Cristi completaban ese equipo.</p>

<p>En 1956 y 1958 integra el seleccionado chileno que compitió con éxito en una gira por Estados Unidos y luego obtiene el séptimo lugar en el Campeonato de Europa y el cuarto puesto en la Copa de las Naciones en Aacheen.</p>

<p>Sus buenas presentaciones le valieron ser nombrado para integrar en 1960 el equipo nacional que se presentaría en los Juegos Olímpicos de Roma. Sin embargo, el terremoto que ese año azotó a la ciudad de Valdivia impidió el viaje de la delegación.</p>

<p>En 1962, Guillermo Aranda deja el mando de la Escuela de Equitación del Ejército y se hace cargo del grupo de instrucción comenzado a participar con mucho éxito en las disciplinas de adiestramiento y prueba completa.</p>

<p>En 1968 y debido a su gran experiencia, don de mando y capacidad organizativa es nombrado jefe del equipo de Adiestramiento de Chile que participó en los Juegos Olímpicos de México. Tres años más tarde repite su función de líder del equipo en los Juegos Panamericanos de Cali, donde Chile obtiene medalla de plata en salto y bronce en adiestramiento.</p>

<p>En 1979 se le concede el retiro absoluto del Ejército y acepta trabajar como maestro en Caracas (Venezuela) en el Club Hípico de esa ciudad. Ese mismo año es designado jefe del equipo juvenil venezolano para participar en Campeonato Americano de Río de Janeiro.</p>

<p>Después de vivir una década en el extranjero, el Ejército de Chile le ofrece contratarlo como profesor titular en la Escuela de Equitación en Quillota para el curso de maestros, lo cual acepta y vuelca toda su experiencia y conocimientos de muchos años.</p>

<p>Durante los últimos años de su vida se radicó en Quillota, siempre montando y apoyando a su Escuela de Equitación, como también a los jinetes militares y civiles que practicaban la cualquier disciplina ecuestre en los picaderos y canchas del instituto, dejando una huella imborrable en todos los jinetes que alguna vez tuvieron contacto de estribos con este gran maestro.</p>

<p>En la década de los 70 y 80 fue profesor del curso de maestros de equitación del ejercito, aportando todas su experiencia y alegría a los jinetes nuevos de la institución. Esta actividad la realizó hasta los últimos días de su vida. </p>

