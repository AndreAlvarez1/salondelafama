			<div class="bio col s12">

<p>Nació en Santiago el 11 de julio de 1941. Desde muy joven se interesó por los caballos y de los animales en general, especialmente, cuando con su familia concurría a veranear a la localidad de “Maitencillo”, donde aprovechaba de montar.</p>

<p>Posteriormente se interesó por el mundo militar, ingresando a la Escuela Matriz de esta institución el 1960.</p>

<p>En su vida de Cadete en la Escuela Militar, recibe las primeras enseñanzas ecuestres formales de parte del maestro de equitación Capitán Carlos López Tapia, quien reafirmó su gusto por el caballo y la equitación.</p>

<p>En 1962 se gradúa como Alférez del arma de caballería y es destinado al regimiento de “Coraceros”, en la ciudad de Viña del Mar, teniendo como maestros al Comandante del regimiento Eugenio Parra y al maestro Capitán Mario Fuentes Fernández, participando en múltiples concursos hípicos de salto en Santiago y provincias, obteniendo importantes triunfos, sin ser maestro de equitación.</p>

<p>En 1964 es destinado a la Escuela de Caballería, donde realiza el curso de Maestros de Equitación, junto a un grupo de destacados jinetes jóvenes de la institución castrense quienes destacaron por sus habilidades como jinetes, en salto y adiestramiento.</p>

<p>En 1968, a sugerencia del Jefe de la Escuela de Equitación del Ejército, le piden que se dedique exclusivamente al adiestramiento, lo cual, le pareció una no muy buena idea y después de pensarlo algunos días aceptó la sugerencia y a partir de ese momento le dedicó todos sus esfuerzos deportivos a esta disciplina.</p>

<p>En 1971 con su caballo “Palmero”, obtiene medalla de plata por equipos y bronce en individual, lo cual, es la última medalla en esta disciplina de nuestra equitación nacional.</p>

<p>En los Juegos Panamericanos de Indianápolis de 1987 obtuvo el cuarto lugar individual, luego en Cuba remató séptimo y en Río de Janeiro el 15° puesto.</p>

<p>Fue campeón sudamericano individual en seis ocasiones y campeón nacional en diferentes categorías, en 87 ocasiones.</p>

<p>En competencias internacionales FEI obtuvo 41 primeros lugares.</p>

<p>En 1973 es elegido por el Círculo de Periodistas Deportivos como el “Mejor Deportista” de ese año.</p>

<p>Es un reconocido juez internacional FEI y ha desempeñado esta función en países de América y Europa.</p>

<p>También ha participado activamente como directivo en nuestra Federación Ecuestre por 12 años y de la FEI por cinco años.</p>

<p>Es fundador de la Comisión de Adiestramiento, la cual ha integrado por más de 12 años.</p>

<p>En relación a la docencia y como maestro de equitación, la ha ejercido durante muchos años en el Ejército de Chile y Club de Polo y Equitación San Cristóbal, actualmente hace clases a alumnos en forma individual el Santiago Paperchase Club. </p>

<p>Recuerda, que en su carrera deportiva ha formado 76 caballos, la gran mayoría de alta competencia, siendo los más importantes “Palmero”, “Veneciano”, “Maracaibo” y “Ticiano”.</p>

<p>Actualmente, vive en la Quinta Región y continúa haciendo clases y montando a sus caballos, con los que participa permanentemente en los concursos organizados por nuestra federación.</p>

<p>Actualmente vive en la V región y continúa haciendo clases y montando a sus caballos, que participa permanentemente en los concursos organizados por nuestra federación.</p>

