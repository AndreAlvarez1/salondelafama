			<div class="bio col s12">

<p>En 1942 cumple su servicio militar como soldado estudiante en el Regimiento de Caballería N° 2 “Cazadores a Caballo”, en la ciudad de Santiago, por su afición a los caballos. </p>

<p>En 1942 ingresa como Subalférez a la Escuela Militar, para seguir la carrera de las armas.</p>

<p>En 1943 se gradúa como Alférez de Ejército en el Arma de Caballería y es destinado al regimiento Guías en Concepción, donde destaca por sus dotes de jinete en las competencias hípicas de todo tipo y amor por los caballos.</p>

<p>En 1944 asciende al grado de Subteniente de Ejército.</p>

<p>En 1944 es designado alumno del Curso de Cañones Antitanques de la Escuela de Infantería.</p>

<p>En 1945 es destinado al Regimiento Cazadores en Santiago, donde continúa destacando como jinete dentro de una generación dorada.</p>

<p>En 1946 asciende al grado de Teniente de Ejército.</p>

<p>En 1948 vuelve a su Regimiento cuna, donde tanto aprendió de sus maestros de equitación, el “Guías” en Concepción.</p>

<p>En 1949 se le concede permiso para contraer matrimonio con la Srta. Emma Anais Metzger Vargas.</p>

<p>En 1949 es designado Alumno del Curso de Tenientes de la Escuela de Caballería en Quillota, donde se gana su paso a un segundo año para Graduarse de “Maestro de 
Equitación” el año 1950.</p>

<p>En 1951 pasa a ser oficial de planta del institucional formador de oficiales de caballería y maestros, integrando el equipo de adiestramiento.</p>

<p>En 1951 participa en el equipo representativo nacional en los Juegos Panamericanos realizados en Buenos Aires, en la disciplina de Concurso Completo, obteniendo medalla de bronce por equipos.</p>

<p>En 1953 se corona campeón sudamericano en adiestramiento, en el concurso Internacional realizado en Santiago, con asistencias de los equipos sudamericanos más importantes.</p>

<p>En 1955 participa con el equipo nacional de adiestramiento en los Juegos Panamericanos de Ciudad de México, logrando la medalla de bronce individual.</p>

<p>En 1957 es comandado al Ejército de Colombia en su calidad de Maestro de Equitación y dos veces campeón panamericano, junto a los capitanes Alberto Labbé y Braulio Saavedra, realizando una fecunda labor en ese país hermano, contrayendo segundas nupcias y teniendo tres hijos de esa nacionalidad.</p>

<p>En 1961 la República de Colombia por su aporte al deporte de ese país, lo distingue con la condecoración “José María Córdoba”, con el grado de Oficial.</p>

<p>En 1961 regresa a Chile, siendo destinado a la Dirección General de Deportes del Estado, a cargo del deporte ecuestre y otras asesorías, efectuando una gran labor, siempre tratando de aportar con su experiencia de maestro y jinete a la equitación nacional.</p>

<p>En 1963 es destinado a la Escuela de Caballería y es nombrado Profesor Militar en el ramo de Didáctica de la Equitación y jefe de equipo de adiestramiento del instituto. Ese mismo año integra el equipo de Chile de adiestramiento en los concursos internacionales en Sao Paulo y Río de Janeiro, obteniendo excelentes resultados.</p>

<p>En 1967 participa en el equipo de adiestramiento nacional en los Juegos Panamericanos de Winnipeg, obteniendo, junto a Patricio Escudero y Mario Díaz, la medalla de oro por equipos y la de bronce individual.</p>

<p>En 1967 asciende al grado de Mayor y se le concede la “Medalla al Mérito Militar de las FF.AA.”, tras 20 años de servicio a la institución.</p>

<p>En 1967, después de 25 años de servicio en el Ejército de Chile, se retira de la institución, trasladándose a su segunda patria, como consideró a Colombia, donde se desempeñó como jefe de equipo en innumerables ocasiones, como también ejerciendo la docencia en el Ejército y clubes colombianos.</p>

<p>En 1972 integra el equipo nacional para asistir a los Juegos Olímpicos de Munich, en Alemania, realizando una gira por Europa, la cual por falta de fondos tuvo que regresar al país y no se pudo concretar la participación en el máximo evento deportivo mundial.</p>

<p>En 2011 fallece en su querida Colombia a los 88 años de edad, siempre contagiando con su alegría y sabios consejos ecuestres, pero nunca perdiendo contacto con Chile, su ejército y su equitación nacional. </p>

<p>Nuestra equitación nacional ante la partida de este extraordinario jinete, hace no mucho tiempo, le reconoce todos sus méritos para integrar el “Hall of Fame”, siendo el único jinete que tiene una medalla panamericana en CCE (1951) y tres en adiestramiento, lo cual sin lugar a dudas es una excepción.</p>

