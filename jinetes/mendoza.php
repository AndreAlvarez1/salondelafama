			<div class="bio col s12">

<p>Nace en la ciudad de Santiago el 11 de septiembre de 1918 y fallece el 13 de septiembre de 1996. Sus padres son Don Atilio Mendoza Valdebenito, profesor en ciencias y alcalde de comuna de La Cisterna y Doña Amalia Durán García, muy aficionada a la música. Fue el penúltimo hijo de 11 hermanos.</p>

<p>Realizó sus estudios primarios y secundarios en el Instituto Nacional, egresando de bachiller en 1937.</p>

<p>Realizó en 1938 su servicio militar como estudiante en el “Regimiento de Caballería Cazadores”, donde demostró sus grandes dotes de jinete y caballero, ganándose el aprecio de sus jefes y compañeros.</p>

<p>Ingresó a la Escuela de Carabineros en 1940, graduándose de Subteniente, después de dos exigentes años de estudio y práctica de este instituto de formación policial de oficiales.</p>

<p>Asciende al grado de Subteniente en 1941, destacando en todas las unidades que sirvió, como instructor de equitación y jinete, obteniendo múltiples triunfos por su institución.</p>

<p>Durante su carrera policial, desempeñó diversas funciones y destinos dentro de la institución, a saber:</p>

<p>1942 Teniente en la ciudad de Molina.</p>

<p>1943 Teniente en la Ciudad de Talca.</p>

<p>Por su destacada formación policial y deportiva es trasladado a la Escuela de Carabineros de Chile, en aquel entonces en la Avenida Antonio Varas, ingresando al equipo representativo institucional, donde inicia su brillante trayectoria como jinete a nivel nacional e internacional.</p>

<p>En 1948, contrae matrimonio con la Sra. Alicia Godoy, con quien tuvo dos hijos, los cuales siempre lo llenaron de felicidad.</p>

<p>En 1951 integra el equipo nacional que logra todas las medallas de salto en los primeros Juegos Panamericanos de 1951 en Buenos Aires. Posteriormente a instancias del Presidente Juan Domingo Perón se organiza un Concurso Hípico Internacional, donde la delegación nacional logra nuevamente todos los lugares de privilegio.</p>

<p>En 1952 es llamado para integrar el equipo nacional que realiza la gira preolímpica en Europa y posteriormente participar en los Juegos Olímpicos de Helsinski. En esta oportunidad tuvo que montar el caballo del jinete Joaquín Larraín, debido a una decisión técnica del jefe de equipo, y por una rebelde cojera de su caballo “Scarface”.</p>

<p>Los resultados no pudieron ser mejores e históricos, concluyendo que fue muy acertada la resolución del General Eduardo Yáñez, jefe de equipo, a pesar de las críticas iniciales de muchos.</p>

<p>Ya de regreso a Chile en el año 1953 obtiene el ascenso al grado de Capitán.</p>

<p>Posteriormente, este brillante jinete se dedicó al adiestramiento, logrando dos medallas panamericanas en los JJ.PP. de Chicago (oro por equipo y bronce individual), siendo uno de los pocos deportistas nacionales e internacionales en la historia ecuestre en tener esta habilidad y capacidad.</p>

<p>En 1959 asciende al grado de mayor y comienza a dejar en forma paulatina la competencia, sin dejar de asistir a ver concursos hípicos y actividades ecuestres de toda una vida, como también haciendo clases a sus amigos y parientes, en la Escuela de Carabineros.</p>

<p>En 1965 asciende al grado de Teniente Coronel y en 1968 a Coronel.</p>

<p>En 1970 es premiado por su larga trayectoria profesional y deportiva, por la institución de Carabineros, con el grado de General, y en 1972, es logra el puesto de General Inspector.</p>

<p>En 1973 asume como máxima autoridad en Carabineros de Chile e integrante de la Junta Militar de Gobierno.</p>

<p>Como integrante de la Junta de Gobierno y máxima autoridad de la institución policial, permanentemente apoyó a la equitación nacional y asistió a los principales eventos ecuestres nacionales e internacionales.</p>

<p>Retirado de toda actividad castrense y política, siempre continuó montando en su querida Escuela de Carabineros, ahora en Maipú, observando y dando consejos a los jóvenes oficiales del equipo institucional.</p>

<p>También se dedicó a la docencia, participando activamente en la creación de la “Universidad de Las Condes” y participando en diferentes fundaciones, junto a su esposa, que tenían como objetivo ir en ayuda de los niños más desposeídos.</p>

<p>Fallece el año 1996, después de una larga y penosa enfermedad en el hospital institucional, en la ciudad de Santiago.</p>

