			<div class="bio col s12">

<p>Nació en Santiago el 25 de marzo de 1917, desde muy joven se interesó por los caballos y demostró gusto por ingresar al Ejército de Chile.</p>
<p>En 1935, ingresa a la Escuela Militar.</p>
<p>En el año 1937 se gradúa como alférez del arma de caballería y es destinado al regimiento de “Coraceros”, en la ciudad de Viña del Mar.</p>
<p>En 1940 es destinado al regimiento de caballería Guías en Concepción, destacando por sus habilidades como jinete, especialmente en adiestramiento.</p>
<p>En 1941, es destinado a la Escuela de Caballería, para realizar el “Curso de Equitación”.</p>
<p>En 1942, ya con el grado de teniente, es destinado al regimiento de caballería “Cazadores” en Santiago, donde ejerció como maestro de equitación, fomentando el “Dressage” entre los jinetes jóvenes de su unidad y a los civiles que montaban en ese regimiento, también siendo campeón nacional en forma sucesiva.</p>
<p>En 1945 contrae matrimonio con la Srta Julia Merino Novoa.</p>
<p>En 1946, es destinado a la Escuela de Caballería en Quillota, donde destaca como maestro y jinete en la disciplina de adiestramiento, logrando sacar muchos caballos para la “Sección de alta Escuela”, del instituto y caballos que representarán a Chile en esta disciplina en las competencias internacionales.</p>
<p>En 1949 con el grado de capitán, participa con el equipo nacional en Lima Perú, obteniendo sus primeros triunfos internacionales.</p>
<p>En 1951, en los primeros Juegos Panamericanos organizados en la Ciudad de Buenos Aires, tuvo una sobresaliente participación, siendo nuestra primera medalla de ORO individual en su caballo “Via Reggio” en los JJPP y Oro por equipos junto a Héctor Clavel. Siendo estos jinetes unos verdaderos hitos y gestores de toda una tradición de la equitación nacional en esta disciplina.</p>
<p>En 1952, Ingresa a la Academia de Guerra del Ejercito como alumno regular, destacando por su calidad académica e intelectual, nunca dejando de participar en adiestramiento y continuar ganando en el deporte Ecuestre que tanto quiso y disfrutó.</p>
<p>En 1956 es destinado a Alemania, a un curso de perfeccionamiento en Warendorff, donde demuestra todas sus condiciones y habilidades en el adiestramiento, en este centro de perfeccionamiento olímpico alemán.</p>
<p>En 1959, es destinado al Cuartel General de la División de Caballería con asiento en Santiago y es elegido director de la FECH.</p>
<p>En 1961 con el grado de Teniente Coronel, es nombrado jefe de la rama de deportes de la Federación Deportiva Militar (FEDEM), cargo ostento hasta el año 1962.</p>
<p>En 1962 Es nombrado Comandante del Regimiento “Granaderos” en la ciudad de Iquique, donde a pesar de la lejanía de la zona central mantuvo un hermoso centro ecuestre, para fomentar la práctica de la equitación de los oficiales, familiares y civiles. </p>
<p>En 1964, es nombrado Director General de Fomento Equino y Remonta, donde aplica todos sus conocimientos y esfuerzos, para mejorar la raza deportiva de nuestros caballos.</p>
<p>En 1965, es nombrado director de la Escuela de Caballería en Quillota, exigiendo en su mando, el máximo a los equipos representativos institucionales.</p>
<p>En 1969, es nombrado Agregado militar en la República de Brasil, cargo que ejerce hasta el año 1970.</p>
<p>En el año 1970, por su larga e impecable trayectoria de oficial y deportista, es ascendido al grado de General de Brigada y condecorado por el Pdte de la República, asumiendo el mando de la División de Caballería con asiento en la ciudad de Valdivia. Aportando con toda su experiencia y pasión a la recuperación de la equitación en el sur de Chile.</p>
<p>En 1971 se le concede el retiro absoluto de la institución a la cual sirvió por más de 35 años.</p>
<p>En situación de retiro, continuó practicando el deporte ecuestre que tanto le apasionó y llenó su vida, en especial el adiestramiento, en las instalaciones de la Escuela Militar. En este centro ecuestre aconsejaba y ayudaba a los jinetes jóvenes y daba el ejemplo montando a diario, a pesar de sus años.</p>
<p>Continuó en esta condición de jinete y maestro hasta su partida en 1973.</p>


