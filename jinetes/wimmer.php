<p>Nace el 29 de marzo de 1982. Sus padres son Karin y Frank Wimmer</p>

<p>Empezó con clases de equitación en Berlín, Alemania, a los cuatro años, en la escuela Onkel Toms Huette con el militar Willy Czarnecky, quien le enseñó desde la base hasta llegar a competir en campeonatos de nivel Intermedio I y II en Adiestramiento y Salto en los años 1992 a 1996. </p>

