<p>Su resultado más destacado en el plano internacional fue el tercer lugar que obtuvo en el CEI4* 120km. del Campeonato Panamericano Adulto de Uruguay 2017. Este logro, que obtuvo a lomos de “EO Salma” es reconocido como el más importante de esta modalidad a nivel nacional.</p>

<p>Su paso por adiestramiento y salto, cuenta, fue fundamental para alcanzar el nivel que posee actualmente en enduro. “El proceso de cambiar el switch de disciplinas como adiestramiento o salto a enduro no fue fácil, ya que son mundos a parte, pero pude sacar lo mejor de todo lo que aprendí para llegar a donde estoy ahora”.</p>


