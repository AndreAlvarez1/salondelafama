			<div class="bio col s12">
		
<p>Nace el 11 de febrero de 1957 en Santiago. Sus padres son Huber Roesgen y María Isabel Camalez Solo de Zaldívar.</p>

<p>Su primer contacto con los caballos en la equitación fue a la edad de 11 años en el Club La Posada en Concepción, mientras acompañaba a clases a unos primos que practicaban y competían por el club. </p>

<p>Sus primeros maestros en la Escuela de Caballería de Quillota fueron Alfonso Anguita y Oscar Coddou y luego recibió la instrucción en Concepción del maestro Carlos Molina.</p>

<p>Su mayor logro deportivo lo obtuvo en agosto de 1987, cuando integró el equipo chileno que obtuvo el tercer lugar en el Concurso Completo de Equitación de los Juegos Panamericanos de Indianápolis. En ese mismo certamen, se alzó como el mejor nacional en la prueba individual, rematando en un destacado cuatro puesto.</p>

<p>Otras actuaciones destacadas de este especialista en el CCE son los títulos sudamericanos que obtuvo en 1987 y 1990, ambos en Buenos Aires; el quinto puesto que alcanzó en la categoría individual en el Campeonato del Mundo de Equitación Militar de España, en 1987 y los primeros lugares que obtuvo en los Campeonatos Nacionales de Perú (1982) y Argentina (1990)</p>

<p>En el plano nacional destacó ganando siete categorías en el Campeonato Nacional de Adiestramiento y siendo campeón por equipos; en salto, en tanto, triunfó el la prueba de potencia a 1,80 metros (1986) y en el Gran Premio Alberto Larraguibel en 1989, 1990 y 1991. En su prueba, el CCE, fue vicecampeón nacional de Caballos Fogueados en 1985, 1986 y 1988.</p>

<p>Entre 1985 y 1989 ofició como profesor del Curso de Maestros de Equitación en las asignaturas de Asiento y Concurso Completo de Equitación. Entre 1990 y  1992, en tanto, fue Maestro y Jefe del Equipo Ecuestre de la Escuela Militar y entre 1995 y 2011 fue Maestro y Gerente de la Rama de Equitación del Club Deportivo de la Universidad Católica de Chile.</p>

