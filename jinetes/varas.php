			<div class="bio col s12">

<p>El general René Varas Asenjo, por su brillante carrera deportiva, es conocido como uno de los más destacados exponentes de la equitación chilena y de igual forma por su calidad humana, donde en el ámbito ecuestre logró ganar importantes laureles para el deporte de este país.</p>

<p>En su paso a través de Carabineros de Chile, de una trayectoria desarrollada a lo largo de 35 años de servicio, supo combinar lo deportivo con lo profesional, lo cual conllevó a que alcanzara el grado de General Inspector de esta prestigiosa institución.</p>

<p>Nace el 19 de mayo de 1936 en la sureña localidad de El Huape, comuna de La Unión, donde de la mano de su padre, don Oscar Varas Rojo, se forma como jinete, entregándole el amor por este noble animal, lamentablemente, perdiéndolo tempranamente a los 14 años.</p>

<p>Estudió en la Escuela N° 1 de esa localidad y posteriormente en el Liceo de La Unión, ingresando a la Escuela de Carabineros de Chile en 1956 a la edad de 19 años, egresando como Sub Teniente en diciembre de 1957.</p>

<p>Tempranamente se distingue por sus habilidades como jinete. El año 1959 integra el curso de Instructores de Equitación en la Escuela de Carabineros. Al año siguiente, en ese mismo establecimiento, junto a otros tres compañeros, realiza el Curso de Maestro de Equitación, obteniendo en ambos, las primeras figuraciones y titulándose con máxima distinción.</p>

<p>Posteriormente integra los equipos ecuestres de la Prefectura de Viña del Mar, año 1961 y 1962 y a contar del año siguiente, el Equipo Ecuestre de la Escuela de Carabineros, donde se mantuvo brillantemente y lleno éxitos hasta 1976.</p>

<p>El año 1964 es llamado por primera vez a la Selección Nacional de Equitación, en la disciplina de salto, por sus brillantes actuaciones logradas en los años previos. Ocupa el cuarto lugar en el selectivo que se realiza en Chile para ir al Campeonato Internacional de Saltos de Buenos Aires. Lamentablemente una lesión de su caballo “Llanero”, no le permite viajar, frustrando su primera incursión en el extranjero.</p>

<p>El año 1966 realiza su primer viaje al extranjero con el equipo nacional, participando en Estados Unidos y Canadá en los cuatro concursos que conformaban el “Horse Show” que se realizan en Harrisburg – Pennsilvania, posteriormente en Washington DC, Nueva York y en Toronto (Canadá), obteniendo sus primeras figuraciones y destacando en el equipo nacional.</p>

<p>En 1967 participa en los V Juegos Panamericanos de Winnipeg - Canadá, donde el equipo nacional ocupa el cuarto lugar.</p>

<p>En 1969 participa en el Tercer Concurso Hípico Oficial Latinoamericano y Campeonato Intercontinental de Salto de Obstáculos, en Viña de Mar, donde se reúnen connotados jinetes internacionales de gran valor a nivel mundial, entre los cuales destacaban exponentes de Alemania, España, Colombia, Brasil, Argentina, México y Chile.</p>

<p>La Revista de Carabineros de la época relata la participación de cada uno de los integrantes del equipo de Chile, Américo Simonetti, el Capitán Guido Larrondo, los tenientes Ricardo Izurieta y René Varas, refiriéndose a ellos como “un núcleo homogéneo, dotado de un estilo impecable, elegante, efectivo y de una destreza en la conducción que sólo mereció elogios. René Varas, logró en el transcurso de esa competencia que el pabellón patrio fuera izado en cuatro oportunidades en el mástil de honor del “Estadio Sausalito”, a los acordes del Himno Nacional.</p>
</p> 

<p>En el año 1970 viaja a Europa en uno de los buques de la Compañía Sudamericana de Vapores el Equipo Ecuestre de Chile, compuesto por la Srta. Bárbara Barone, el juvenil Guillermo Franke y los Tenientes Guido Larrondo y René Varas, estando a cargo de la delegación el Presidente de la Federación Ecuestre de Chile, General de Ejército, don Eduardo Yáñez Zabala. Desembarcan en el Puerto de Hamburgo, Alemania, después de 38 días de navegación, llegando los caballos en muy mal estado al Viejo Continente. Posteriormente, a sólo 15 días de su arribo, tienen su primera participación en el concurso más duro de Europa, el Derby de Hamburgo, pese a ello, “Llanero” y René Varas, ocuparon el noveno lugar, ubicación no superada hasta la fecha por otro chileno.</p>

<p>Lo anterior fue el inicio de un largo recorrido de más de cuatro meses como preparativo para la participación del Equipo de Chile en el Campeonato del Mundo que se realizaría en La Baule, Francia. Chile cumplió un gran desempeño con sus dos binomios en cancha, el Capitán Guido Larrondo Jara en “Romeral” y el Teniente René Varas en “Quintral”, quien obtuvo finalmente el 10° lugar.</p>

<p>En el año 1971, participa en los VI Juegos Panamericanos de Cali, Colombia, obteniendo medalla de bronce en equipos, junto a Eugenio Lavín, Guido Larrondo y Bárbara Barone, y el cuarto lugar individual, montando a “Quintral”, siendo esta medalla la última de la disciplina de saltos en Juegos Panamericanos.</p>

<p>Este mismo año es elegido por el Círculo de Periodistas Deportivos de Chile, como el Mejor de los Mejores, por todos sus logros deportivos y ser un ejemplo para los deportistas nacionales.</p>

<p>En el año 1972 la Federación Ecuestre de Chile y el Comité Olímpico determinan viajar a Europa en una gira preolímpica, con vistas a los Juegos de Munich, Alemania. Es así que compiten intensamente en un lapso de seis meses con excelentes resultados, como el título del Gran Prix de Luxemburgo, Copa Manpower. </p>

<p>El capitán René Varas, además, resulta ganador indiscutido en el Gran Prix de Evian, en Francia montando a “Quintral” y con el mismo ejemplar, en el Campeonato de Potencia de Alemania Federal, resulta ganador con 2,20 metros de altura, ante la admiración de los jinetes europeos.</p>

<p>En los Juegos Olímpicos de Múnich, René Varas tiene el honor de ser el abanderado del Equipo Olímpico de Chile, designación del Comité Olímpico de Chile, momento que para este jinete ha sido unos de los más emocionantes en su carrera deportiva, más aún terminando 10° en la clasificación individual, siendo el mejor latinoamericano en este megaevento.</p>

<p>En octubre de 1973 viaja a Sao Paulo, Brasil, junto a Américo Simonetti y los Tenientes de Ejercito Ricardo Miranda y Víctor Contador, logrando Varas para Chile tres primeros lugares, al adjudicarse la prueba de potencia con 2,07 metros en “Quintral”, además de ganar con “Llanero” el recorrido a 1,50 y la prueba “Copa de Naciones”, superando entre otros a Argentina y Brasil.</p>

<p>En el año 1975 se desarrolla el IV Latinoamericano de Equitación en la Escuela de Carabineros de Santiago y en el Estado Sausalito de Viña de Mar. Destacan los logros alcanzados en la primera locación: Latinoamericano Internacional con primero y tercer lugar de René Varas con “Quintral” y “Cyrano”; logra triunfos también en las Pruebas de Velocidad y Conducción con “Quintral”, en la de Novicios con “Hualqui” y en la de Barreras Internacional, obtiene el segundo lugar también con “Quintral”.</p>

<p>En el año 1976, nuevamente el Comité Olímpico en base a los resultados de años anteriores, determina la participación de Chile en los Juegos Olímpicos de Montreal, Canadá, para lo cual se inicia gira a Europa, viajando nuevamente en la Compañía Sudamericana de Vapores. El equipo nacional estaba integrado por Américo Simonetti, el Capitán de Ejército Daniel Walker, el Teniente Víctor Contador y el Mayor de Carabineros, René Varas. Participan durante cuatro meses con muy buenos resultados en Alemania, Francia, Bélgica, España y Suiza, donde se destaca el triunfo en el Campeonato de Potencia de Alemania Federal de René Varas, ganando con “Quintral” con 2,20 metros de altura. </p>

<p>En 1977 participó en el Campeonato Internacional de saltos con obstáculos, en Lima, Perú, con sus caballos “Quintral” y “Lanin”, obteniendo el Premio Federico Caprille de la Embajada de Italia, destinado al Mejor Jinete Clasificado en las Pruebas Internacionales, por acumulación de eventos ganados.</p>

<p>Dos años más tarde participa en el Campeonato Internacional Latinoamericano realizado en Chile con destacados resultados, distinguiéndose en forma especial al obtener el primer y segundo lugar en la Copa de Naciones, con sus caballos “Lanin” y “Quintral”, respectivamente.</p>

<p>Habiendo sido llamado a retiro el año 1992, volvió a las canchas ecuestres representando a Carabineros de Chile, mientras la institución mantuvo el Equipo de la Prefectura de Carabineros de Concepción.</p>

<p>Este destacado jinete de Carabineros de Chile y representante del deporte ecuestre de nacional ganó en cuatro oportunidades el Campeonato de Chile de Salto todo competidor y en varias otras fue vicecampeón.</p>

<p>Por su destacada trayectoria fue considerado en dos oportunidades en los rankings internacionales dentro de los 10 mejores jinetes del mundo. El año 1971, la ciudad de La Unión le confiere la distinción de Hijo Ilustre, donde hoy una avenida lleva su nombre por larga trayectoria deportiva y ejemplo para la juventud. El año 1986 recibe la misma distinción de la ciudad de Osorno.</p>

<p>Asimismo, el diario El Mercurio, durante tres años, le entregó el Premio Julio Moreno Toledano como el Mejor Deportista Ecuestre de Chile.</p>

<p>En 1973 el Círculo de Periodista Deportivos, la Dirección de Deportes del Estado y el COCH, le otorgan el Premio al “Mérito Deportivo”.</p>

<p>Recibe en el año 1994 el Premio Internacional “Centenario del Comité Olímpico Mundial”, otorgado por don Juan Antonio Samaranch, presidente del COI, por su notable actuación como deportista ecuestre.</p>

<p>En el año 1996, el Círculo de Periodistas Deportivos de Chile lo designa como el Mejor Deportista Antiguo de Chile, premio que recibe en el Teatro Teletón.</p>

<p>En 1997 es nominado como Jefe del Equipo de Chile para el Campeonato Sudamericano Adulto a desarrollarse en Buenos Aires, Argentina, honor que declina por compromisos personales, ya que se encontraba en ese tiempo dedicado a sacar adelante la reciente Escuela de Equitación, que fundara en la ciudad de Concepción.</p>

<p>El año 2014, estando presente la Directiva del Comité Olímpico y del Instituto Nacional del Deporte, recibe de la Federación Ecuestre de Chile el Premio a la Trayectoria Ecuestre, donde son también homenajeados, Bárbara Barone y Américo Simonetti.</p>

<p>El año 2016, la Asociación Ecuestre de Concepción, en la ciudad de Chillán, Concurso Hípico Nacional de Senior, le entrega por intermedio de su Presidente, el Premio a la Trayectoria Deportiva como Jinete y Maestro de Equitación, en reconocimiento a su fecunda labor ecuestre.</p>

<p>Desde entonces, se ha preocupado por transmitir sus experiencias, formando jinetes en diversos clubes de Carabineros, creando además dos nuevos Centros Ecuestres en las ciudades de Osorno y Llanquihue, a la vez que remozó el Centro Ecuestre de Carabineros de “Las Quilas” de Temuco.</p>

<p>Desde hace 24 años se encuentra radicado en la ciudad de Concepción donde sigue ligado a la equitación a través de la “Escuela de Equitación René Varas Asenjo”, entregando su experiencia en la formación de nuevos jinetes y en la difusión de su pasión, especialmente en la juventud y niños de la VIII Región, distinguiéndose su escuela en el campo de la “equinoterapia”.</p>

<p>Este gran jinete guarda un recuerdo emotivo de gratitud hacia sus maestros de equitación, tanto de Carabineros como del Ejército, entre los cuales destacaron por su Institución, el Capitán Luis Labbé Álvarez y los Generales César Mendoza Durán y Alberto Montesinos Caro, quienes volcaron en él no solamente sus experiencias ecuestres, sino también lo formaron como hombre y futuro Jefe de esta Institución.</p>

<p>Por parte del Ejército, le es imposible olvidar al General Eduardo Yáñez Zabala, al Coronel, Rafael Montti Roa, al Comandante Guillermo Aranda Parra y al Mayor Amaro Pérez de Castro; de todos ellos recibió los mejores consejos, tanto como jinete como en el aspecto directivo y así lo manifestó muchas veces, dando gracias por las enseñanzas recibidas, las que sin duda vivirán en su recuerdo de por vida.</p>

<p>Al hacer un análisis de su trayectoria deportiva, René Varas considera que la mejor forma de demostrar el agradecimiento a su padre y maestros, es haber transmitido la pasión que ellos le inculcaron a sus hijos y familia.</p>

<p>Sus cuatro descendientes heredaron su vocación y tres de ellos lo hicieron compitiendo activamente. Rodrigo, el mayor, es abogado y Maestro de Equitación; integró los Equipos de la Escuela de Carabineros saltando, además, en un Campeonato Internacional en Ecuador; hoy compite y hace clases en la Escuela de Equitación, que lleva el nombre de su padre en Concepción.</p>

<p>Marcela Paz, abogada, fue una destacada jinete infantil, integrando el Equipo de Chile en Campeonatos Internacionales con notable éxito; René Nicolás, Médico y Teniente Primero de Sanidad de la Armada de Chile, integró la selección de Chile como jinete infantil durante varios años, siendo su mayor orgullo haber participado en el Campeonato del Mundo que se realizó en Chile el año 2000.</p>

<p>También destaca su sobrino, Jorge Aros Varas, al que le inculcó la misma pasión por el caballo y este hermoso deporte, quien desde muy joven se dedicó a estudiar la difícil tarea de ser juez en la disciplina de salto.</p>

<p>En su trayectoria ligada a la equitación, de los muchos caballos fiscales y particulares que tuvo la suerte de montar, inolvidable le resulta “Quintral”; el mejor, también “Llanero”, “Llifen”, “Lanin”, “California”, “Estaño”, “Pasos Perdidos”, “Luz de Luna”, “Barcelona” y “Armonía”.</p>

<p>De la misma manera se destaca en su trayectoria el personal de Carabineros de Chile, quienes le acompañaron en su vida deportiva en calidad de ordenanzas, cuidando a los nobles animales, pudiendo destacar a los Suboficiales de Carabineros, Joaquín Oyarzún Contreras, Edilfonso San Martín Barra, Sergio Orellana Leal, Víctor Mardones, Rigoberto Veloso y Jorge Soto del Solar.</p>
